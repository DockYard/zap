    }

    /// Build the expression `Option.None`. The shape mirrors the
    /// canonical parser AST for the same surface text — a two-part
    /// `struct_ref` whose parts are `[Option, None]` — so HIR's
    /// tagged-union-variant-from-struct_ref path handles it
    /// (including context-driven inference of the parametric
    /// instantiation from the surrounding expected-type stack). The
    /// alternate `field_access(struct_ref([Option]), None)` shape that
    /// earlier desugar passes used skips that inference and lowers as
    /// a bare enum literal — a u32 tag — which fails to coerce
    /// against a `union(enum)` Option specialization at the use site.
    fn buildOptionNoneExpr(self: *Desugarer, span: ast.SourceSpan) !*const ast.Expr {
        const interner_mut: *ast.StringInterner = self.interner;
        const option_id = try interner_mut.intern("Option");
        const none_id = try interner_mut.intern("None");
        const parts = try self.allocSlice(ast.StringId, &.{ option_id, none_id });
        return try self.create(ast.Expr, .{
            .struct_ref = .{
                .meta = .{ .span = span },
                .name = .{ .parts = parts, .span = span },
            },
        });
    }

    /// Build a call expression `Option.Some(<payload>)`. The callee
    /// uses the canonical parser shape — a two-part `struct_ref` with
    /// parts `[Option, Some]` — so HIR's call-with-tagged-union-variant
    /// path matches at line `.call =>` in `buildExpr` (which is where
    /// context-driven inference of the parametric instantiation from
    /// `expected_type_stack` lives).
    fn buildOptionSomeCall(self: *Desugarer, span: ast.SourceSpan, payload: *const ast.Expr) !*const ast.Expr {
        const interner_mut: *ast.StringInterner = self.interner;
        const option_id = try interner_mut.intern("Option");
        const some_id = try interner_mut.intern("Some");
        const parts = try self.allocSlice(ast.StringId, &.{ option_id, some_id });
        const callee = try self.create(ast.Expr, .{
            .struct_ref = .{
                .meta = .{ .span = span },
                .name = .{ .parts = parts, .span = span },
            },
        });
        const args = try self.allocSlice(*const ast.Expr, &.{payload});
        return try self.create(ast.Expr, .{
            .call = .{
                .meta = .{ .span = span },
                .callee = callee,
                .args = args,
            },
        });
    }

    fn desugarStruct(self: *Desugarer, mod: *const ast.StructDecl) !ast.StructDecl {
        self.pending_helpers = .empty;
        // Set current struct scope for import resolution
        if (self.graph) |g| {
            self.current_struct_scope = g.findStructScope(mod.name);
        }
        defer self.current_struct_scope = null;
        var new_items: std.ArrayList(ast.StructItem) = .empty;
        for (mod.items) |item| {
            try new_items.append(self.allocator, try self.desugarStructItem(item));
        }
        // Append helper functions generated by for comprehensions
        for (self.pending_helpers.items) |helper| {
            try new_items.append(self.allocator, helper);
        }
        try self.appendSynthesizedRunIfNeeded(mod, &new_items);
        return .{
            .meta = mod.meta,
            .name = mod.name,
            .parent = mod.parent,
            // `type_params` MUST flow through — desugarStruct's job is to
            // rewrite items (lift for-comprehension helpers, synthesize
            // the `run/0` helper for struct-level expressions, etc.),
            // NOT to strip declaration-level metadata. Without this
            // pass-through, `pub struct Box(T) { ... }` would emerge
            // from the desugar phase with an empty type_params slice,
            // and the type-checker's `registerUserTypes` would treat it
            // as a concrete non-parametric struct — every `T` inside
            // the body would then be reported as "I cannot find a type
            // named T". Surfaced by Phase 1.2's Test G `pub error
            // DeserializeError(T)` and by `zap run` + parametric struct
            // fixtures in script mode (the harness path that runs
            // `desugarProgram`).
            .type_params = mod.type_params,
            .items = try new_items.toOwnedSlice(self.allocator),
            .fields = mod.fields,
            .is_private = mod.is_private,
        };
    }

    fn appendSynthesizedRunIfNeeded(
        self: *Desugarer,
        mod: *const ast.StructDecl,
        items: *std.ArrayList(ast.StructItem),
    ) !void {
        const run_name = try self.interner.intern("run");
        var struct_level_exprs: std.ArrayList(*const ast.Expr) = .empty;

        for (items.items) |item| {
            switch (item) {
                .function => |func| {
                    if (self.functionHasArity(func, run_name, 0)) return;
                },
                .priv_function => |func| {
                    if (self.functionHasArity(func, run_name, 0)) return;
                },
                .struct_level_expr => |expr| try struct_level_exprs.append(self.allocator, expr),
                else => {},
            }
        }

        if (struct_level_exprs.items.len == 0) return;

        const statements = try self.allocator.alloc(ast.Stmt, struct_level_exprs.items.len);
        for (struct_level_exprs.items, 0..) |expr, index| {
            statements[index] = .{ .expr = expr };
        }

        const string_type_name = try self.nativeStringTypeName();
        const return_type = try self.create(ast.TypeExpr, .{
            .name = .{
                .meta = mod.meta,
                .name = string_type_name,
                .args = &.{},
            },
        });

        const clauses = try self.allocator.alloc(ast.FunctionClause, 1);
        clauses[0] = .{
            .meta = mod.meta,
            .params = &.{},
            .return_type = return_type,
            .refinement = null,
            .body = statements,
        };

        const run_decl = try self.create(ast.FunctionDecl, .{
            .meta = mod.meta,
            .name = run_name,
            .name_expr = null,
            .clauses = clauses,
            .visibility = .public,
        });
        try items.append(self.allocator, .{ .function = run_decl });
    }

    fn functionHasArity(self: *const Desugarer, func: *const ast.FunctionDecl, name: ast.StringId, arity: usize) bool {
        _ = self;
        if (func.name != name) return false;
        for (func.clauses) |clause| {
            if (clause.params.len == arity) return true;
        }
        return false;
    }

    fn nativeStringTypeName(self: *Desugarer) !ast.StringId {
        if (self.graph) |graph| {
            if (graph.nativeTypeStructName(.string)) |name| return name;
        }
        return self.interner.intern("String");
    }

    fn desugarTopItem(self: *Desugarer, item: ast.TopItem) !ast.TopItem {
        return switch (item) {
            .function => |func| .{ .function = try self.desugarFunctionDecl(func) },
            .priv_function => |func| .{ .priv_function = try self.desugarFunctionDecl(func) },
            .macro => |mac| .{ .macro = try self.desugarMacroDecl(mac) },
            .priv_macro => |mac| .{ .priv_macro = try self.desugarMacroDecl(mac) },
            else => item,
        };
    }

    fn desugarStructItem(self: *Desugarer, item: ast.StructItem) !ast.StructItem {
        return switch (item) {
            .function => |func| .{ .function = try self.desugarFunctionDecl(func) },
            .priv_function => |func| .{ .priv_function = try self.desugarFunctionDecl(func) },
            .macro => |mac| .{ .macro = try self.desugarMacroDecl(mac) },
            .priv_macro => |mac| .{ .priv_macro = try self.desugarMacroDecl(mac) },
            .struct_level_expr => |expr| .{ .struct_level_expr = try self.desugarExpr(expr) },
            else => item,
        };
    }

    /// Desugar a macro declaration. Identical to `desugarFunctionDecl`
    /// except for setting `in_macro_body` so that nested `for`
    /// comprehensions are not lifted into `__for_N` private functions.
    /// Macro bodies are compile-time templates evaluated by the macro
    /// engine; the `for` is handled by `forComprehensionIntrinsic` in
    /// `src/macro_eval.zig`, so a runtime helper would be both useless
    /// and a source of spurious "function not found" diagnostics for
    /// the comptime-only intrinsics in the body.
    fn desugarMacroDecl(self: *Desugarer, mac: *const ast.FunctionDecl) !*const ast.FunctionDecl {
        const prev = self.in_macro_body;
        self.in_macro_body = true;
        defer self.in_macro_body = prev;
        return self.desugarFunctionDecl(mac);
    }

    // ============================================================
    // Function declaration desugaring
    // ============================================================

    fn desugarFunctionDecl(self: *Desugarer, func: *const ast.FunctionDecl) !*const ast.FunctionDecl {
        var new_clauses: std.ArrayList(ast.FunctionClause) = .empty;
        for (func.clauses) |clause| {
            // The tail (result) expression of a clause whose declared
            // return type is a `fn`-type is an escaping position: a
            // capturing closure returned there outlives the frame and must
            // box as `Callable`. When the tail is in fact a capturing
            // closure, also rewrite the `fn(A) -> B` return type to
            // `Callable({A}, B)` so the boxed value type-checks against the
            // return slot and `maybeBoxAsProtocol` auto-boxes it. A
            // non-capturing tail closure keeps the bare `fn`-type (Gap E's
            // direct return path) — its return type is left unchanged.
            const return_is_fn = clause.return_type != null and clause.return_type.?.* == .function;
            // Record this clause's parameter type annotations into the
            // capture-type environment so a closure nested in the body can
            // type a captured parameter concretely. Save/restore around
            // the body so the bindings don't leak to sibling clauses or
            // the enclosing scope.
            const saved_capture_env = try self.capture_type_env.clone(self.allocator);
            defer {
                self.capture_type_env.deinit(self.allocator);
                self.capture_type_env = saved_capture_env;
            }
            for (clause.params) |param| {
                try self.recordParamCaptureTypes(param.pattern, param.type_annotation);
            }
            const body = if (clause.body) |b|
                try self.desugarFunctionClauseBody(b, return_is_fn)
            else
                null;
            const return_type = if (return_is_fn and body != null and self.tailIsBoxedClosure(body.?))
                try self.callableTypeFromFunctionType(clause.return_type.?)
            else
                clause.return_type;
            try new_clauses.append(self.allocator, .{
                .meta = clause.meta,
                .params = clause.params,
                .return_type = return_type,
                .refinement = if (clause.refinement) |r| try self.desugarExpr(r) else null,
                .body = body,
                // Carry the declared `raises` row through desugar unchanged:
                // type expressions in the row are not rewritten by desugar
                // (mirrors `return_type`), so pass the slice by reference.
                .raises = clause.raises,
            });
        }
        return try self.create(ast.FunctionDecl, .{
            .meta = func.meta,
            .name = func.name,
            .clauses = try new_clauses.toOwnedSlice(self.allocator),
            .visibility = func.visibility,
        });
    }

    /// Desugar a function clause body, marking the tail (result)
    /// expression as escaping when `return_is_fn` so a capturing closure
    /// returned directly is boxed as `Callable`. Only the final statement
    /// — and only when it is a bare expression statement — is the result
    /// position; every earlier statement desugars in the ambient
    /// (non-escaping) context.
    fn desugarFunctionClauseBody(self: *Desugarer, stmts: []const ast.Stmt, return_is_fn: bool) anyerror![]const ast.Stmt {
        if (!return_is_fn or stmts.len == 0) return try self.desugarBlock(stmts);
        var new_stmts: std.ArrayList(ast.Stmt) = .empty;
        for (stmts, 0..) |stmt, index| {
            const is_tail = index == stmts.len - 1;
            if (is_tail and stmt == .expr) {
                try new_stmts.append(self.allocator, .{ .expr = try self.desugarExprEscaping(stmt.expr) });
            } else {
                try new_stmts.append(self.allocator, try self.desugarStmt(stmt));
            }
        }
        return new_stmts.toOwnedSlice(self.allocator);
    }

    /// True iff the body's tail (result) statement is a
    /// `%__closure_N{...}` construction synthesized by the escaping-closure
    /// desugar — i.e. the closure was boxed as a `Callable`. Used to decide
    /// whether the clause's `fn`-typed return annotation should be rewritten
    /// to `Callable`.
    fn tailIsBoxedClosure(self: *const Desugarer, stmts: []const ast.Stmt) bool {
        if (stmts.len == 0) return false;
        const tail = stmts[stmts.len - 1];
        if (tail != .expr) return false;
        return self.exprIsSynthesizedClosureStruct(tail.expr);
    }

    // ============================================================
    // Block desugaring
    // ============================================================

    fn desugarBlock(self: *Desugarer, stmts: []const ast.Stmt) anyerror![]const ast.Stmt {
        var new_stmts: std.ArrayList(ast.Stmt) = .empty;
        for (stmts) |stmt| {
            try new_stmts.append(self.allocator, try self.desugarStmt(stmt));
        }
        return new_stmts.toOwnedSlice(self.allocator);
    }

    fn desugarStmt(self: *Desugarer, stmt: ast.Stmt) anyerror!ast.Stmt {
        return switch (stmt) {
            .expr => |expr| .{ .expr = try self.desugarExpr(expr) },
            .assignment => |assign| .{
                .assignment = try self.create(ast.Assignment, .{
                    .meta = assign.meta,
                    .pattern = assign.pattern,
                    .value = try self.desugarExpr(assign.value),
                }),
            },
            .function_decl => |func| .{ .function_decl = try self.desugarFunctionDecl(func) },
            .macro_decl => |mac| .{ .macro_decl = try self.desugarMacroDecl(mac) },
            .import_decl => stmt,
            .attribute => |attr| .{
                .attribute = try self.create(ast.AttributeDecl, .{
                    .meta = attr.meta,
                    .name = attr.name,
                    .type_expr = attr.type_expr,
                    .value = if (attr.value) |value| try self.desugarExpr(value) else null,
                }),
            },
        };
    }

    // ============================================================
    // Expression desugaring
    // ============================================================

    fn desugarExpr(self: *Desugarer, expr: *const ast.Expr) anyerror!*const ast.Expr {
        switch (expr.*) {
            // Pipe: desugared during macro expansion (Kernel.|> macro)
            .pipe => |pe| {
                const lhs = try self.desugarExpr(pe.lhs);
                const rhs = try self.desugarExpr(pe.rhs);
                return try self.create(ast.Expr, .{
                    .pipe = .{ .meta = pe.meta, .lhs = lhs, .rhs = rhs },
                });
            },

            // Unwrap: expr! → optional force-unwrap (panics if nil)
            // String interpolation: "hello #{name}" → "hello " <> to_string(name)
            .string_interpolation => |si| {
                return self.desugarStringInterpolation(&si);
            },

            // Recurse into compound expressions
            .binary_op => |bo| {
                const new_lhs = try self.desugarExpr(bo.lhs);
                const new_rhs = try self.desugarExpr(bo.rhs);
                // `value in collection` desugars to `Membership.member?(collection, value)`
                // so the in operator dispatches through the protocol like every
                // other type-polymorphic operation. The HIR builder rewrites
                // the protocol call to the matching impl based on `collection`'s
                // inferred type. Note arg order: collection first, then value.
                if (bo.op == .in_op) {
                    const interner_mut: *ast.StringInterner = @constCast(self.interner);
                    const proto_name = try interner_mut.intern("Membership");
                    const fn_name = try interner_mut.intern("member?");
                    const parts = try self.allocator.alloc(ast.StringId, 1);
                    parts[0] = proto_name;
                    const mod_ref = try self.create(ast.Expr, .{
                        .struct_ref = .{ .meta = bo.meta, .name = .{ .parts = parts, .span = bo.meta.span } },
                    });
                    const callee = try self.create(ast.Expr, .{
                        .field_access = .{ .meta = bo.meta, .object = mod_ref, .field = fn_name },
                    });
                    return try self.create(ast.Expr, .{
                        .call = .{
                            .meta = bo.meta,
                            .callee = callee,
                            .args = try self.allocSlice(*const ast.Expr, &.{ new_rhs, new_lhs }),
                        },
                    });
                }
                return try self.create(ast.Expr, .{
                    .binary_op = .{
                        .meta = bo.meta,
                        .op = bo.op,
                        .lhs = new_lhs,
                        .rhs = new_rhs,
                    },
                });
            },
            .unary_op => |uo| {
                return try self.create(ast.Expr, .{
                    .unary_op = .{
                        .meta = uo.meta,
                        .op = uo.op,
                        .operand = try self.desugarExpr(uo.operand),
                    },
                });
            },
            .call => |call| {
                var new_args: std.ArrayList(*const ast.Expr) = .empty;
                for (call.args) |arg| {
                    // A function reference passed as a call argument is a
                    // callable value: eta-expand it into a forwarding
                    // anonymous function (see `etaExpandFunctionRefArg`).
                    // Every other argument desugars normally.
                    if (arg.* == .function_ref) {
                        try new_args.append(self.allocator, try self.etaExpandFunctionRefArg(arg.function_ref));
                    } else {
                        try new_args.append(self.allocator, try self.desugarExpr(arg));
                    }
                }
                const desugared_args = try new_args.toOwnedSlice(self.allocator);

                // Elixir-style import resolution: bare calls like `sleep(150)`
                // are rewritten to qualified calls `Kernel.sleep(150)` when the
                // function is found in an imported struct (including auto-imported
                // Kernel). This happens at the AST level, before HIR lowering.
                if (call.callee.* == .var_ref) {
                    const name = call.callee.var_ref.name;
                    const arity: u32 = @intCast(call.args.len);
                    if (self.resolveImportedStruct(name, arity)) |mod_name| {
                        // Rewrite: var_ref("sleep") → field_access(struct_ref("Kernel"), "sleep")
                        const mod_ref_expr = try self.create(ast.Expr, .{
                            .struct_ref = .{
                                .meta = call.meta,
                                .name = mod_name,
                            },
                        });
                        const qualified_callee = try self.create(ast.Expr, .{
                            .field_access = .{
                                .meta = call.meta,
                                .object = mod_ref_expr,
                                .field = name,
                            },
                        });
                        return try self.create(ast.Expr, .{
                            .call = .{
                                .meta = call.meta,
                                .callee = qualified_callee,
                                .args = desugared_args,
                            },
                        });
                    }
                }

                const callee = try self.desugarExpr(call.callee);
                return try self.create(ast.Expr, .{
                    .call = .{
                        .meta = call.meta,
                        .callee = callee,
                        .args = desugared_args,
                    },
                });
            },
            .anonymous_function => |anon| {
                // A capturing closure literal in an ESCAPING position
                // (struct-field value, collection element, `fn`-typed
                // return tail) is rewritten into a boxed `Callable`
                // existential: a synthesized `struct __closure_N` holding
                // the captures + `impl Callable({params}, ret)` whose
                // `call` method is the body, and the literal becomes a
                // `%__closure_N{captures}` construction. This is the
                // currently-failing case (a capture-environment struct
                // cannot fit a bare `fn`-ptr slot). Non-capturing closures
                // and capturing closures consumed as direct call arguments
                // stay on the existing #201/Gap E direct path.
                // Box decision (escape-driven representation):
                //   - a HETEROGENEOUS-COLLECTION element → box regardless
                //     of capture (uniform element representation);
                //   - a BOXED-SLOT field/map value → box regardless of
                //     capture (the slot's type IS the boxed `Callable`;
                //     a non-capturing closure becomes an EMPTY-env box);
                //   - any other ESCAPING position (the `fn`-typed
                //     RETURN-TAIL) → box only when it CAPTURES (Gap E keeps
                //     a non-capturing returned closure as a bare fn-ptr).
                // Everything else (direct call argument, non-escaping
                // local) → the #201/Gap E direct path, untouched.
                const box_in_collection = self.closure_escapes_collection;
                const box_in_boxed_slot = self.closure_escapes_boxed_slot;
                const box_capturing_slot = self.closure_escapes and try self.closureLiteralCaptures(anon.decl);
                if (box_in_collection or box_in_boxed_slot or box_capturing_slot) {
                    return try self.desugarEscapingClosure(anon);
                }
                // The closure's OWN body is not an escaping context for the
                // expressions inside it; reset the flags while recursing so
                // a non-escaping inner closure is not spuriously boxed.
                const saved_escapes = self.closure_escapes;
                const saved_escapes_collection = self.closure_escapes_collection;
                const saved_escapes_boxed_slot = self.closure_escapes_boxed_slot;
                self.closure_escapes = false;
                self.closure_escapes_collection = false;
                self.closure_escapes_boxed_slot = false;
                defer {
                    self.closure_escapes = saved_escapes;
                    self.closure_escapes_collection = saved_escapes_collection;
                    self.closure_escapes_boxed_slot = saved_escapes_boxed_slot;
                }
                return try self.create(ast.Expr, .{
                    .anonymous_function = .{
                        .meta = anon.meta,
                        .decl = try self.desugarFunctionDecl(anon.decl),
                    },
                });
            },
            // if_expr and cond_expr are expanded to case by the
            // macro engine (Kernel macros / special forms) before desugaring.
            // They should not reach this point.
            .case_expr => |ce| {
                var new_clauses: std.ArrayList(ast.CaseClause) = .empty;
                for (ce.clauses) |clause| {
                    try new_clauses.append(self.allocator, .{
                        .meta = clause.meta,
                        .pattern = clause.pattern,
                        .type_annotation = clause.type_annotation,
                        .guard = if (clause.guard) |g| try self.desugarExpr(g) else null,
                        .body = try self.desugarBlock(clause.body),
                    });
                }
                return try self.create(ast.Expr, .{
                    .case_expr = .{
                        .meta = ce.meta,
                        .scrutinee = try self.desugarExpr(ce.scrutinee),
                        .clauses = try new_clauses.toOwnedSlice(self.allocator),
                    },
                });
            },
            .block => |blk| {
                return try self.create(ast.Expr, .{
                    .block = .{
                        .meta = blk.meta,
                        .stmts = try self.desugarBlock(blk.stmts),
                    },
                });
            },

            // Error pipe: pass through to HIR builder. Transform function handlers
            // into block handlers so the handler call is a top-level expression.
            .error_pipe => |ep| {
                const chain = try self.desugarExpr(ep.chain);
                const handler: ast.ErrorHandler = switch (ep.handler) {
                    .function => |func| blk: {
                        // ~> handle_error() → ~> { __err -> handle_error(__err) }
                        // The unmatched value is injected as the first argument.
                        const desugared_func = try self.desugarExpr(func);
                        const err_name = try self.interner.intern("__err");
                        const err_ref = try self.create(ast.Expr, .{
                            .var_ref = .{ .meta = ep.meta, .name = err_name },
                        });

                        // Inject __err as first argument into the handler call
                        const call_expr = if (desugared_func.* == .call) ce: {
                            const call = desugared_func.call;
                            var new_args: std.ArrayList(*const ast.Expr) = .empty;
                            try new_args.append(self.allocator, err_ref);
                            for (call.args) |arg| {
                                try new_args.append(self.allocator, arg);
                            }
                            break :ce try self.create(ast.Expr, .{
                                .call = .{
                                    .meta = call.meta,
                                    .callee = call.callee,
                                    .args = try new_args.toOwnedSlice(self.allocator),
                                },
                            });
                        } else
                            // Bare function ref: wrap as call with __err
                            try self.create(ast.Expr, .{
                                .call = .{
                                    .meta = ep.meta,
                                    .callee = desugared_func,
                                    .args = try self.allocSlice(*const ast.Expr, &.{err_ref}),
                                },
                            });

                        const body = try self.allocSlice(ast.Stmt, &.{.{ .expr = call_expr }});
                        const pattern = try self.create(ast.Pattern, .{
                            .bind = .{ .meta = ep.meta, .name = err_name },
                        });
                        const clauses = try self.allocSlice(ast.CaseClause, &.{
                            .{ .meta = ep.meta, .pattern = pattern, .type_annotation = null, .guard = null, .body = body },
                        });
                        break :blk .{ .block = clauses };
                    },
                    .block => |clauses| blk: {
                        var new_clauses: std.ArrayList(ast.CaseClause) = .empty;
                        for (clauses) |clause| {
                            try new_clauses.append(self.allocator, .{
                                .meta = clause.meta,
                                .pattern = clause.pattern,
                                .type_annotation = clause.type_annotation,
                                .guard = if (clause.guard) |guard| try self.desugarExpr(guard) else null,
                                .body = try self.desugarBlock(clause.body),
                            });
                        }
                        break :blk .{ .block = try new_clauses.toOwnedSlice(self.allocator) };
                    },
                };
                return try self.create(ast.Expr, .{
                    .error_pipe = .{
                        .meta = ep.meta,
                        .chain = chain,
                        .handler = handler,
                    },
                });
            },

            // Map update: %{map | key: val, ...} → Updatable.update chains.
            // Routed through the protocol so any user type implementing
            // `Updatable.update/3` can opt in to the `%{x | k: v}` syntax.
            // The HIR builder rewrites the protocol call to the concrete
            // impl based on `result`'s inferred type.
            .map => |me| {
                if (me.update_source) |source| {
                    var result = try self.desugarExpr(source);
                    const interner_mut: *ast.StringInterner = @constCast(self.interner);
                    const proto_name = try interner_mut.intern("Updatable");
                    const fn_name = try interner_mut.intern("update");
                    for (me.fields) |field| {
                        const parts = try self.allocator.alloc(ast.StringId, 1);
                        parts[0] = proto_name;
                        const mod_ref = try self.create(ast.Expr, .{
                            .struct_ref = .{ .meta = me.meta, .name = .{ .parts = parts, .span = me.meta.span } },
                        });
                        const callee = try self.create(ast.Expr, .{
                            .field_access = .{ .meta = me.meta, .object = mod_ref, .field = fn_name },
                        });
                        result = try self.create(ast.Expr, .{
                            .call = .{
                                .meta = me.meta,
                                .callee = callee,
                                .args = try self.allocSlice(*const ast.Expr, &.{
                                    result,
                                    try self.desugarExpr(field.key),
                                    try self.desugarExpr(field.value),
                                }),
                            },
                        });
                    }
                    return result;
                }
                return expr;
            },

            // For comprehension → block with recursive helper function + call
            .for_expr => |fe| {
                return self.desugarForExpr(&fe);
            },

            // List cons expression → recurse into head and tail
            .list_cons_expr => |lce| {
                // A list cons stores both head and tail into the list — an
                // escaping position for a capturing closure head.
                return try self.create(ast.Expr, .{
                    .list_cons_expr = .{
                        .meta = lce.meta,
                        .head = try self.desugarExprEscapingCollection(lce.head),
                        .tail = try self.desugarExprEscapingCollection(lce.tail),
                    },
                });
            },

            // Struct expression: desugar update syntax %Name{old | field: val}
            .struct_expr => |se| {
                // Struct-field values are an escaping position: a capturing
                // closure stored in a `fn`-typed field must box as
                // `Callable` so the field can carry the captured
                // environment (a bare `fn`-ptr cannot).
                var new_fields: std.ArrayList(ast.StructField) = .empty;
                for (se.fields) |field| {
                    try new_fields.append(self.allocator, .{
                        .name = field.name,
                        // A struct-field value is a BOXED-SLOT position: a
                        // `fn`-typed field stores the boxed `Callable`
                        // representation, so a closure literal here boxes
                        // regardless of capture (a bare fn-ptr cannot fit
                        // the `ProtocolBox` slot).
                        .value = try self.desugarExprEscapingBoxedSlot(field.value),
                    });
                }

                if (se.update_source) |source| {
                    const desugared_source = try self.desugarExpr(source);
                    return try self.desugarStructUpdate(se, desugared_source, new_fields.items);
                }

                return try self.create(ast.Expr, .{
                    .struct_expr = .{
                        .meta = se.meta,
                        .struct_name = se.struct_name,
                        // Explicit type arguments at the literal
                        // (`%Box(i64){...}`) MUST flow through the
                        // desugar — they drive per-instantiation
                        // monomorphization at the type-checker layer.
                        // The desugar's job here is to rewrite the
                        // `update_source` and the field VALUES; the
                        // `(i64)` header is preserved verbatim.
                        // Dropping it silently turned `%Box(i64){...}`
                        // into `%Box{...}` (provided_arity == 0) and
                        // every parametric field reported as
                        // `expects {type_var}` because the
                        // SubstitutionMap had no binding for the
                        // formal type-var.
                        .type_args = se.type_args,
                        .type_args_parens_present = se.type_args_parens_present,
                        .update_source = null,
                        .fields = try new_fields.toOwnedSlice(self.allocator),
                    },
                });
            },

            .range => |re| {
                // Rewrite `start..end[..step]` into `%Range{start: ..., end: ..., step: ...}`.
                // Range is a Zap struct (lib/range.zap); the compiler doesn't need
                // a dedicated AST shape — desugaring it here keeps HIR oblivious to
                // the struct's identity. Step validation (must be > 0) lives in
                // the type checker since this rewrite always runs before that pass.
                const new_start = try self.desugarExpr(re.start);
                const new_end = try self.desugarExpr(re.end);
                const step_expr: *const ast.Expr = if (re.step) |s|
                    try self.desugarExpr(s)
                else
                    try self.create(ast.Expr, .{
                        .int_literal = .{ .meta = re.meta, .value = 1 },
                    });

                const interner_mut: *ast.StringInterner = @constCast(self.interner);
                const range_name = try interner_mut.intern("Range");
                const start_name = try interner_mut.intern("start");
                const end_name = try interner_mut.intern("end");
                const step_name = try interner_mut.intern("step");

                const fields = try self.allocator.alloc(ast.StructField, 3);
                fields[0] = .{ .name = start_name, .value = new_start };
                fields[1] = .{ .name = end_name, .value = new_end };
                fields[2] = .{ .name = step_name, .value = step_expr };

                const parts = try self.allocator.alloc(ast.StringId, 1);
                parts[0] = range_name;

                return self.create(ast.Expr, .{
                    .struct_expr = .{
                        .meta = re.meta,
                        .struct_name = .{ .parts = parts, .span = re.meta.span },
                        .update_source = null,
                        .fields = fields,
                    },
                });
            },

            // ----- Compound expressions: recurse so transformations
            //       (pipe rewriting, string interpolation, etc.) inside
            //       compound shapes are not silently lost.

            .tuple => |te| {
                var new_elements: std.ArrayList(*const ast.Expr) = .empty;
                for (te.elements) |elem| {
                    try new_elements.append(self.allocator, try self.desugarExpr(elem));
                }
                return try self.create(ast.Expr, .{
                    .tuple = .{
                        .meta = te.meta,
                        .elements = try new_elements.toOwnedSlice(self.allocator),
                    },
                });
            },
            .list => |le| {
                // List elements are an escaping position: a value stored
                // in a list outlives the expression that produced it, so a
                // capturing closure element must box as `Callable`. This is
                // the canonical heterogeneous-collection case
                // (`[fn(i64) -> i64]`).
                var new_elements: std.ArrayList(*const ast.Expr) = .empty;
                for (le.elements) |elem| {
                    try new_elements.append(self.allocator, try self.desugarExprEscapingCollection(elem));
                }
                return try self.create(ast.Expr, .{
                    .list = .{
                        .meta = le.meta,
                        .elements = try new_elements.toOwnedSlice(self.allocator),
                    },
                });
            },
            .field_access => |fa| {
                return try self.create(ast.Expr, .{
                    .field_access = .{
                        .meta = fa.meta,
                        .object = try self.desugarExpr(fa.object),
                        .field = fa.field,
                    },
                });
            },
            .unwrap => |uw| {
                return try self.desugarUnwrap(try self.desugarExpr(uw.expr), uw.meta);
            },
            // `try_rescue` survives desugar like `case`: the
            // node stays intact so the type-checker can compute its
            // exhaustiveness, discharge the rescued error types from the
            // enclosing `raises` row, and so HIR can perform the
            // error-union/handler lowering (which needs `ret`/error-return
            // terminators that surface AST cannot express). Only the
            // sub-expressions/statements are desugared here.
            .try_rescue => |tr| {
                var new_clauses: std.ArrayList(ast.CaseClause) = .empty;
                for (tr.rescue_clauses) |clause| {
                    try new_clauses.append(self.allocator, .{
                        .meta = clause.meta,
                        .pattern = clause.pattern,
                        .type_annotation = clause.type_annotation,
                        .guard = if (clause.guard) |g| try self.desugarExpr(g) else null,
                        .body = try self.desugarBlock(clause.body),
                    });
                }
                const new_after: ?[]const ast.Stmt = if (tr.after_block) |cleanup|
                    try self.desugarBlock(cleanup)
                else
                    null;
                return try self.create(ast.Expr, .{
                    .try_rescue = .{
                        .meta = tr.meta,
                        .body = try self.desugarBlock(tr.body),
                        .rescue_clauses = try new_clauses.toOwnedSlice(self.allocator),
                        .after_block = new_after,
                    },
                });
            },
            .with_expr => |we| {
                // `with` is fully desugared to nested `case` during macro
                // expansion (src/macro.zig `withToNestedCase`), so it
                // normally never reaches the desugarer. This arm keeps the
                // exhaustive switch total and stays correct if the
                // desugarer is ever run on un-expanded AST: recurse into
                // the step exprs, the do-body, and the else-clause bodies,
                // rebuilding a well-formed `with_expr`.
                var new_steps: std.ArrayList(ast.WithStep) = .empty;
                for (we.steps) |step| {
                    try new_steps.append(self.allocator, .{
                        .meta = step.meta,
                        .pattern = step.pattern,
                        .type_annotation = step.type_annotation,
                        .expr = try self.desugarExpr(step.expr),
                    });
                }
                const new_else: ?[]const ast.CaseClause = if (we.else_clauses) |clauses| blk: {
                    var new_clauses: std.ArrayList(ast.CaseClause) = .empty;
                    for (clauses) |clause| {
                        try new_clauses.append(self.allocator, .{
                            .meta = clause.meta,
                            .pattern = clause.pattern,
                            .type_annotation = clause.type_annotation,
                            .guard = if (clause.guard) |g| try self.desugarExpr(g) else null,
                            .body = try self.desugarBlock(clause.body),
                        });
                    }
                    break :blk try new_clauses.toOwnedSlice(self.allocator);
                } else null;
                return try self.create(ast.Expr, .{
                    .with_expr = .{
                        .meta = we.meta,
                        .steps = try new_steps.toOwnedSlice(self.allocator),
                        .do_body = try self.desugarBlock(we.do_body),
                        .else_clauses = new_else,
                    },
                });
            },
            .panic_expr => |pe| {
                return try self.create(ast.Expr, .{
                    .panic_expr = .{
                        .meta = pe.meta,
                        .message = try self.desugarExpr(pe.message),
                    },
                });
            },
            .raise_expr => |re| {
                // Phase 1.4 `raise <value>` lowering: rewrite to a call of
                // `Kernel.do_raise(<value>)`, the Error-aware abort. The
                // `do_raise/1` family (lib/kernel.zap) is multi-clause:
                //
                //   * `do_raise(message :: String)` — the `raise "string"`
                //     shorthand path: wraps the message in
                //     `%RuntimeError{message: ...}` and recurses. This lives
                //     in Zap (not the desugar) so it handles ANY String-typed
                //     value (`raise "x" <> y`, `raise some_string`), not just
                //     bare string literals.
                //   * `do_raise(error_value :: Error)` — extracts
                //     `Error.message`/`Error.kind` and aborts non-zero.
                //
                // Building the call here in desugar — BEFORE the type-checker
                // runs — lets it resolve through the normal call-inference +
                // multi-clause-dispatch + monomorphization path (synthesising
                // it post-typecheck in HIR would leave the callee untyped and
                // fall back to a closure call). The `raise_expr` wrapper
                // survives only so the type-checker can record the raised
                // error type into the enclosing function's inferred `raises`
                // row; HIR lowers it transparently.
                const desugared_value = try self.desugarExpr(re.value);
                // Eagerly normalise a bare string LITERAL / interpolation to
                // `%RuntimeError{message: ...}` so the common `raise "msg"`
                // case carries a concrete `RuntimeError` value (this makes
                // the type-checker record `RuntimeError` — not `String` — in
                // the inferred `raises` row). Non-literal String-typed values
                // (`raise x <> y`) are left as-is and handled by the
                // `do_raise(String)` clause at runtime.
                const error_value: *const ast.Expr = switch (desugared_value.*) {
                    .string_literal, .string_interpolation => try self.wrapStringInRuntimeError(desugared_value, re.meta),
                    else => desugared_value,
                };
                const do_raise_call = try self.buildDoRaiseCall(error_value, re.meta);
                return try self.create(ast.Expr, .{
                    .raise_expr = .{
                        .meta = re.meta,
                        .value = do_raise_call,
                    },
                });
            },
            .type_annotated => |ta| {
                return try self.create(ast.Expr, .{
                    .type_annotated = .{
                        .meta = ta.meta,
                        .expr = try self.desugarExpr(ta.expr),
                        .type_expr = ta.type_expr,
                    },
                });
            },

            // ----- Leaf nodes — no recursion, no transformation.
            //       Listed explicitly so adding a new Expr variant
            //       triggers a switch-exhaustiveness compile error here
            //       and forces a deliberate decision instead of silently
            //       falling through `else => return expr`.

            .int_literal,
            .float_literal,
            .string_literal,
            .atom_literal,
            .bool_literal,
            .nil_literal,
            .var_ref,
            .struct_ref,
            .intrinsic,
            .attr_ref,
            .binary_literal,
            .function_ref,
            // Poison sentinel (Phase 4.b): a leaf parse-error placeholder —
            // pass it through untouched (the compile is already failing).
            .poison,
            => return expr,

            // ----- Macro-internal AST nodes — these are placeholders
            //       inside `quote { ... }` bodies and should NEVER be
            //       desugared. The macro engine consumes them.

            .quote_expr,
            .unquote_expr,
            .unquote_splicing_expr,
            => return expr,

            // ----- Control-flow forms expanded to `case` by the macro
            //       engine before desugaring runs. Reaching this point
            //       indicates a pipeline ordering bug, not a normal
            //       desugaring case — keep them passthrough as a
            //       defensive measure but flag for future tightening.

            .if_expr,
            .cond_expr,
            => return expr,
        }
    }

    /// Expand struct update syntax: %Name{old | x: new_x}
    /// Looks up the struct definition to get all field names, then generates
    /// a complete struct init where unchanged fields use old.field_name.
    // ============================================================
    // Escaping closure → `struct __closure_N` + `impl Callable` desugar
    //
    // A capturing closure literal in an escaping position (struct-field
    // value, collection element, `fn`-typed return tail) is rewritten into
    // a boxed `Callable` existential. This mirrors the `pub error` desugar
    // (`desugarErrorDecl`): a synthesized struct + impl pair joins the
    // program, and the surface expression becomes a struct construction
    // that downstream HIR/IR auto-boxes (`maybeBoxAsProtocol`) wherever it
    // flows into a `protocol_box(Callable)` slot.
    //
    //   fn(x :: i64) -> i64 { x + n }      # captures `n`
    //
    // becomes
    //
    //   struct __closure_N { n :: i64 }
    //   pub impl Callable({i64}, i64) for __closure_N {
    //     fn call(self :: __closure_N, args :: {i64}) -> i64 {
    //       x = args.0          # one prelude binding per closure param
    //       x + self.n          # captures rewritten to self.<field>
    //     }
    //   }
    //
    // and the literal becomes `%__closure_N{ n: n }`. Capture-field
    // ordering is the deterministic first-reference order of the free
    // variables in the body (see `collectClosureCaptures`).
    // ============================================================

    /// Desugar an expression that occupies an escaping position. Sets the
    /// `closure_escapes` flag for the duration of the recursion so a
    /// capturing closure literal directly in this position is boxed as
    /// `Callable`, then restores the previous flag.
    fn desugarExprEscaping(self: *Desugarer, expr: *const ast.Expr) anyerror!*const ast.Expr {
        const saved = self.closure_escapes;
        self.closure_escapes = true;
        defer self.closure_escapes = saved;
        return self.desugarExpr(expr);
    }

    /// Desugar an expression in a BOXED-SLOT position — a struct-literal
    /// field value or a map value whose declared type is the boxed
    /// `Callable({A}, R)` representation of `fn(A) -> R`. A closure literal
    /// here is boxed as `Callable` regardless of capture (the slot has one
    /// representation, the `ProtocolBox`; a bare fn-ptr cannot fit it),
    /// exactly as a collection element is. Sets both `closure_escapes` and
    /// `closure_escapes_boxed_slot`; restores both afterward.
    fn desugarExprEscapingBoxedSlot(self: *Desugarer, expr: *const ast.Expr) anyerror!*const ast.Expr {
        const saved = self.closure_escapes;
        const saved_boxed_slot = self.closure_escapes_boxed_slot;
        self.closure_escapes = true;
        self.closure_escapes_boxed_slot = true;
        defer {
            self.closure_escapes = saved;
            self.closure_escapes_boxed_slot = saved_boxed_slot;
        }
        return self.desugarExpr(expr);
    }

    /// Desugar an expression in a heterogeneous-collection element
    /// position (list/tuple element, map value). A closure literal here is
    /// boxed as `Callable` regardless of capture so the collection's
    /// element type is uniform. Restores both escape flags afterward.
    fn desugarExprEscapingCollection(self: *Desugarer, expr: *const ast.Expr) anyerror!*const ast.Expr {
        const saved = self.closure_escapes;
        const saved_collection = self.closure_escapes_collection;
        self.closure_escapes = true;
        self.closure_escapes_collection = true;
        defer {
            self.closure_escapes = saved;
            self.closure_escapes_collection = saved_collection;
        }
        return self.desugarExpr(expr);
    }

    /// True iff `expr` is a `%__closure_N{...}` construction synthesized by
    /// `desugarEscapingClosure`. Recognized by the `__closure_` name
    /// prefix on a single-part struct reference.
    fn exprIsSynthesizedClosureStruct(self: *const Desugarer, expr: *const ast.Expr) bool {
        if (expr.* != .struct_expr) return false;
        const name = expr.struct_expr.struct_name;
        if (name.parts.len != 1) return false;
        return std.mem.startsWith(u8, self.interner.get(name.parts[name.parts.len - 1]), "__closure_");
    }

    /// Build the `Callable({P0, P1, ...}, R)` type expression that a boxed
    /// closure's slot must carry, from the closure's `fn(P0, P1) -> R` type
    /// expression. The argument tuple is the closure's parameter types
    /// (the empty tuple `{}` for a zero-arg closure — a genuine
    /// zero-element tuple distinct from `void`).
    fn callableTypeFromFunctionType(self: *Desugarer, fn_type: *const ast.TypeExpr) !*const ast.TypeExpr {
        const ft = fn_type.function;
        return self.buildCallableTypeExpr(ft.params, ft.return_type, fn_type.getMeta().span);
    }

    /// Construct a `Callable({args...}, result)` type expression. `args` is
    /// the closure parameter type list packed into a brace-tuple; `result`
    /// is the closure return type.
    fn buildCallableTypeExpr(
        self: *Desugarer,
        args: []const *const ast.TypeExpr,
        result: *const ast.TypeExpr,
        span: ast.SourceSpan,
    ) !*const ast.TypeExpr {
        const interner_mut: *ast.StringInterner = self.interner;
        const callable_id = try interner_mut.intern("Callable");
        const args_tuple = try self.create(ast.TypeExpr, .{
            .tuple = .{ .meta = .{ .span = span }, .elements = try self.allocSlice(*const ast.TypeExpr, args) },
        });
        const type_args = try self.allocSlice(*const ast.TypeExpr, &.{ args_tuple, result });
        return try self.create(ast.TypeExpr, .{
            .name = .{ .meta = .{ .span = span }, .name = callable_id, .args = type_args },
        });
    }

    /// Syntactic free-variable test: does the closure literal reference any
    /// value binding from an enclosing scope (a capture)? A closure with no
    /// captures is a bare code pointer with no environment to manage, so it
    /// stays on the direct path (Gap E / #201) and is never boxed.
    ///
    /// Conservative and sound: collects the closure's bound names (its
    /// parameters and its own `=` assignment bindings) and walks the body
    /// for `var_ref`s outside that set. Module/struct references are dotted
    /// (`Foo.bar`) or bare calls the desugar rewrites to `Kernel.bar`, so a
    /// surviving bare lowercase `var_ref` is a genuine captured value
    /// (Zap requires module qualification for cross-module functions).
    fn closureLiteralCaptures(self: *Desugarer, decl: *const ast.FunctionDecl) !bool {
        var captures: std.ArrayList(ast.StringId) = .empty;
        defer captures.deinit(self.allocator);
        try self.collectClosureCaptures(decl, &captures);
        return captures.items.len > 0;
    }

    /// Record a parameter's declared type into the capture-type
    /// environment, keyed by the bound name. Only a simple `bind` pattern
    /// (the common case `name :: Type`) contributes a typed entry;
    /// destructuring patterns bind component names whose individual types
    /// aren't directly available, so they are left to fall back to `any`.
    fn recordParamCaptureTypes(
        self: *Desugarer,
        pattern: *const ast.Pattern,
        type_annotation: ?*const ast.TypeExpr,
    ) !void {
        const ann = type_annotation orelse return;
        switch (pattern.*) {
            .bind => |b| try self.capture_type_env.put(self.allocator, b.name, ann),
            .paren => |p| try self.recordParamCaptureTypes(p.inner, ann),
            else => {},
        }
    }

    /// Collect the free variables (captures) of a closure literal in
    /// deterministic first-reference order. A name is a capture iff it is
    /// referenced by a `var_ref` in the body and is neither one of the
    /// closure's parameters nor a name the body itself binds via `=`.
    /// Names already collected are skipped so each capture appears once.
    fn collectClosureCaptures(
        self: *Desugarer,
        decl: *const ast.FunctionDecl,
        captures: *std.ArrayList(ast.StringId),
    ) !void {
        if (decl.clauses.len == 0) return;
        const clause = decl.clauses[0];
        // Names bound inside the closure (not captures): its parameters
        // plus any locals it binds via `=`. Built per-closure.
        var bound = std.AutoHashMap(ast.StringId, void).init(self.allocator);
        defer bound.deinit();
        for (clause.params) |param| {
            try self.collectPatternBindings(param.pattern, &bound);
        }
        const body = clause.body orelse return;
        for (body) |stmt| {
            try self.collectStmtBoundNames(stmt, &bound);
        }
        for (body) |stmt| {
            try self.collectStmtCaptures(stmt, &bound, captures);
        }
    }

    /// Record every binding name introduced by a pattern (a `bind`, or the
    /// sub-patterns of tuple/list/cons/struct/binary/paren/pin patterns).
    fn collectPatternBindings(
        self: *Desugarer,
        pattern: *const ast.Pattern,
        bound: *std.AutoHashMap(ast.StringId, void),
    ) anyerror!void {
        switch (pattern.*) {
            .bind => |b| try bound.put(b.name, {}),
            .wildcard, .literal => {},
            .tuple => |t| for (t.elements) |sub| try self.collectPatternBindings(sub, bound),
            .list => |l| for (l.elements) |sub| try self.collectPatternBindings(sub, bound),
            .list_cons => |lc| {
                for (lc.heads) |sub| try self.collectPatternBindings(sub, bound);
                try self.collectPatternBindings(lc.tail, bound);
            },
            .map => |m| for (m.fields) |field| try self.collectPatternBindings(field.value, bound),
            .struct_pattern => |sp| for (sp.fields) |field| try self.collectPatternBindings(field.pattern, bound),
            .pin => {},
            .paren => |p| try self.collectPatternBindings(p.inner, bound),
            .binary => |bp| for (bp.segments) |seg| {
                if (seg.value == .pattern) try self.collectPatternBindings(seg.value.pattern, bound);
            },
            .tagged_union_variant => |tv| if (tv.payload) |payload| try self.collectPatternBindings(payload, bound),
        }
    }

    /// Record names a statement binds: assignment LHS patterns and nested
    /// closure-free `=` bindings. (A nested closure introduces its own
    /// scope; its parameters are not visible to the outer body, so they
    /// are not added here — only the outer assignment LHS names are.)
    fn collectStmtBoundNames(
        self: *Desugarer,
        stmt: ast.Stmt,
        bound: *std.AutoHashMap(ast.StringId, void),
    ) anyerror!void {
        switch (stmt) {
            .assignment => |assign| try self.collectPatternBindings(assign.pattern, bound),
            else => {},
        }
    }

    /// Walk a statement collecting captured `var_ref`s (those outside
    /// `bound`).
    fn collectStmtCaptures(
        self: *Desugarer,
        stmt: ast.Stmt,
        bound: *std.AutoHashMap(ast.StringId, void),
        captures: *std.ArrayList(ast.StringId),
    ) anyerror!void {
        switch (stmt) {
            .expr => |e| try self.collectExprCaptures(e, bound, captures),
            .assignment => |assign| try self.collectExprCaptures(assign.value, bound, captures),
            else => {},
        }
    }

    /// Walk an expression collecting captured `var_ref`s. A bare
    /// `var_ref` whose name is not in `bound` is appended to `captures`
    /// (once). Nested closures are walked with their own parameters added
    /// to a forked `bound` set so the inner closure's params do not count
    /// as outer captures, while a free variable the inner closure pulls
    /// from the OUTER scope is still recorded as an outer capture.
    fn collectExprCaptures(
        self: *Desugarer,
        expr: *const ast.Expr,
        bound: *std.AutoHashMap(ast.StringId, void),
        captures: *std.ArrayList(ast.StringId),
    ) anyerror!void {
        switch (expr.*) {
            .var_ref => |vr| {
                if (bound.contains(vr.name)) return;
                for (captures.items) |existing| {
                    if (existing == vr.name) return;
                }
                try captures.append(self.allocator, vr.name);
            },
            .binary_op => |bo| {
                try self.collectExprCaptures(bo.lhs, bound, captures);
                try self.collectExprCaptures(bo.rhs, bound, captures);
            },
            .unary_op => |uo| try self.collectExprCaptures(uo.operand, bound, captures),
            .call => |call| {
                try self.collectExprCaptures(call.callee, bound, captures);
                for (call.args) |arg| try self.collectExprCaptures(arg, bound, captures);
            },
            .field_access => |fa| try self.collectExprCaptures(fa.object, bound, captures),
            .pipe => |pe| {
                try self.collectExprCaptures(pe.lhs, bound, captures);
                try self.collectExprCaptures(pe.rhs, bound, captures);
            },
            .unwrap => |uw| try self.collectExprCaptures(uw.expr, bound, captures),
            .tuple => |te| for (te.elements) |elem| try self.collectExprCaptures(elem, bound, captures),
            .list => |le| for (le.elements) |elem| try self.collectExprCaptures(elem, bound, captures),
            .list_cons_expr => |lce| {
                try self.collectExprCaptures(lce.head, bound, captures);
                try self.collectExprCaptures(lce.tail, bound, captures);
            },
            .map => |me| {
                if (me.update_source) |src| try self.collectExprCaptures(src, bound, captures);
                for (me.fields) |field| {
                    try self.collectExprCaptures(field.key, bound, captures);
                    try self.collectExprCaptures(field.value, bound, captures);
                }
            },
            .struct_expr => |se| {
                if (se.update_source) |src| try self.collectExprCaptures(src, bound, captures);
                for (se.fields) |field| try self.collectExprCaptures(field.value, bound, captures);
            },
            .range => |re| {
                try self.collectExprCaptures(re.start, bound, captures);
                try self.collectExprCaptures(re.end, bound, captures);
                if (re.step) |s| try self.collectExprCaptures(s, bound, captures);
            },
            .if_expr => |ie| {
                try self.collectExprCaptures(ie.condition, bound, captures);
                for (ie.then_block) |stmt| try self.collectStmtCaptures(stmt, bound, captures);
                if (ie.else_block) |eb| for (eb) |stmt| try self.collectStmtCaptures(stmt, bound, captures);
            },
            .case_expr => |ce| {
                try self.collectExprCaptures(ce.scrutinee, bound, captures);
                for (ce.clauses) |clause| {
                    if (clause.guard) |g| try self.collectExprCaptures(g, bound, captures);
                    for (clause.body) |stmt| try self.collectStmtCaptures(stmt, bound, captures);
                }
            },
            .block => |blk| for (blk.stmts) |stmt| try self.collectStmtCaptures(stmt, bound, captures),
            .anonymous_function => |anon| {
                // Walk the nested closure with its parameters added to a
                // forked bound set so its own params are not outer
                // captures, but free variables it pulls from the outer
                // scope still register as outer captures.
                if (anon.decl.clauses.len == 0) return;
                const inner = anon.decl.clauses[0];
                var inner_bound = try bound.clone();
                defer inner_bound.deinit();
                for (inner.params) |param| try self.collectPatternBindings(param.pattern, &inner_bound);
                if (inner.body) |inner_body| {
                    for (inner_body) |stmt| try self.collectStmtBoundNames(stmt, &inner_bound);
                    for (inner_body) |stmt| try self.collectStmtCaptures(stmt, &inner_bound, captures);
                }
            },
            .type_annotated => |ta| try self.collectExprCaptures(ta.expr, bound, captures),
            .error_pipe => |ep| try self.collectExprCaptures(ep.chain, bound, captures),
            .try_rescue => |tr| {
                for (tr.body) |stmt| try self.collectStmtCaptures(stmt, bound, captures);
                for (tr.rescue_clauses) |clause| {
                    for (clause.body) |stmt| try self.collectStmtCaptures(stmt, bound, captures);
                }
                if (tr.after_block) |after| for (after) |stmt| try self.collectStmtCaptures(stmt, bound, captures);
            },
            .raise_expr => |re| try self.collectExprCaptures(re.value, bound, captures),
            // Literals, struct/function/attr references, intrinsics, quote
            // forms, and poison carry no captured value variables.
            else => {},
        }
    }

    /// Rewrite a capturing closure literal into a boxed `Callable`
    /// existential: synthesize `struct __closure_N { <captures> }` +
    /// `pub impl Callable({params}, ret) for __closure_N { fn call(...) }`,
    /// register both with the program, and return the
    /// `%__closure_N{ field: capturedVar, ... }` construction expression.
    fn desugarEscapingClosure(self: *Desugarer, anon: ast.AnonymousFunctionExpr) anyerror!*const ast.Expr {
        const interner_mut: *ast.StringInterner = self.interner;
        const decl = anon.decl;
        const span = anon.meta.span;
        if (decl.clauses.len == 0) {
            @panic("desugarEscapingClosure: closure literal has no clause");
        }
        const clause = decl.clauses[0];

        // ----- 1. Determine captures (deterministic first-reference order).
        var captures: std.ArrayList(ast.StringId) = .empty;
        defer captures.deinit(self.allocator);
        try self.collectClosureCaptures(decl, &captures);

        // ----- 2. Resolve param types and the return type. A closure
        // literal carries `:: T` annotations on every parameter and a
        // `-> R` return type (the surface form `fn(x :: A) -> R { ... }`).
        // When an annotation is absent we fall back to `any` so the impl is
        // still well-formed; the type checker then refines via inference.
        const param_types = try self.allocator.alloc(*const ast.TypeExpr, clause.params.len);
        for (clause.params, 0..) |param, idx| {
            param_types[idx] = param.type_annotation orelse try self.anyTypeExpr(span);
        }
        const return_type = clause.return_type orelse try self.anyTypeExpr(span);

        // ----- 3. Synthesize the closure struct name `__closure_N`.
        const closure_index = self.closure_counter;
        self.closure_counter += 1;
        const struct_name_text = try std.fmt.allocPrint(self.allocator, "__closure_{d}", .{closure_index});
        const struct_name_id = try interner_mut.intern(struct_name_text);
        const struct_name: ast.StructName = .{
            .parts = try self.allocSlice(ast.StringId, &.{struct_name_id}),
            .span = span,
        };

        // ----- 4. Build the struct's capture fields (one per captured
        // variable, in capture order). Each field's type is the captured
        // binding's type; the desugar cannot resolve types, so each field
        // is annotated `any` and the type checker infers the concrete
        // capture type from the construction site's argument. Deterministic
        // ordering is the capture-collection order.
        const fields = try self.allocator.alloc(ast.StructFieldDecl, captures.items.len);
        for (captures.items, 0..) |capture_name, idx| {
            // Type each capture field from the enclosing scope's declared
            // type for that binding when known (a concrete type the
            // codegen can lay out); fall back to `any` otherwise (the
            // type checker then backfills the field type from the single
            // construction site — see `backfillClosureFieldType`).
            const field_type = self.capture_type_env.get(capture_name) orelse try self.anyTypeExpr(span);
            fields[idx] = .{
                .meta = .{ .span = span },
                .name = capture_name,
                .type_expr = field_type,
                .default = null,
            };
        }

        // ----- 5. Build the `call` method body:
        //   - one prelude binding `p_i = args.<i>` per closure parameter,
        //     so the original body's parameter references resolve unchanged;
        //   - the original body with captured `var_ref`s rewritten to
        //     `self.<field>` (params are already in scope via the prelude).
        const args_name_id = try interner_mut.intern("args");
        const self_name_id = try interner_mut.intern("self");
        var capture_set = std.AutoHashMap(ast.StringId, void).init(self.allocator);
        defer capture_set.deinit();
        for (captures.items) |capture_name| try capture_set.put(capture_name, {});

        var call_body: std.ArrayList(ast.Stmt) = .empty;
        // Prelude: bind each parameter name to its tuple slot. Only
        // `bind`-pattern params get a prelude binding; a non-bind param
        // pattern (destructuring) is bound directly against the tuple slot
        // value via an assignment whose LHS is the original pattern.
        for (clause.params, 0..) |param, idx| {
            const slot = try self.tupleIndexAccess(args_name_id, idx, span);
            try call_body.append(self.allocator, .{
                .assignment = try self.create(ast.Assignment, .{
                    .meta = .{ .span = span },
                    .pattern = param.pattern,
                    .value = slot,
                }),
            });
        }
        const original_body = clause.body orelse &[_]ast.Stmt{};
        for (original_body) |stmt| {
            try call_body.append(self.allocator, try self.rewriteCaptureStmt(stmt, self_name_id, &capture_set));
        }

        // ----- 6. The `call` method signature:
        //   pub fn call(self :: __closure_N, args :: {P0, P1, ...}) -> R
        const self_type = try self.create(ast.TypeExpr, .{
            .name = .{ .meta = .{ .span = span }, .name = struct_name_id, .args = &.{} },
        });
        const args_tuple_type = try self.create(ast.TypeExpr, .{
            .tuple = .{ .meta = .{ .span = span }, .elements = try self.allocSlice(*const ast.TypeExpr, param_types) },
        });
        const self_param: ast.Param = .{
            .meta = .{ .span = span },
            .pattern = try self.create(ast.Pattern, .{ .bind = .{ .meta = .{ .span = span }, .name = self_name_id } }),
            .type_annotation = self_type,
            .ownership = .shared,
            .ownership_explicit = false,
            .default = null,
        };
        const args_param: ast.Param = .{
            .meta = .{ .span = span },
            .pattern = try self.create(ast.Pattern, .{ .bind = .{ .meta = .{ .span = span }, .name = args_name_id } }),
            .type_annotation = args_tuple_type,
            .ownership = .shared,
            .ownership_explicit = false,
            .default = null,
        };
        const call_name_id = try interner_mut.intern("call");
        const call_clauses = try self.allocator.alloc(ast.FunctionClause, 1);
        call_clauses[0] = .{
            .meta = .{ .span = span },
            .params = try self.allocSlice(ast.Param, &.{ self_param, args_param }),
            .return_type = return_type,
            .refinement = null,
            // The closure body may itself contain pipes, interpolations, or
            // nested escaping closures: desugar it.
            .body = try self.desugarBlock(try call_body.toOwnedSlice(self.allocator)),
            .raises = clause.raises,
        };
        const call_decl = try self.create(ast.FunctionDecl, .{
            .meta = .{ .span = span },
            .name = call_name_id,
            .name_expr = null,
            .clauses = call_clauses,
            .visibility = .public,
        });

        // ----- 7. Emit the synthesized struct declaration into the
        // pending list (flushed into `program.structs`).
        const struct_value: ast.StructDecl = .{
            .meta = .{ .span = span },
            .name = struct_name,
            .parent = null,
            .type_params = &.{},
            .items = &.{},
            .fields = fields,
            .is_private = false,
        };
        try self.pending_closure_structs.append(self.allocator, struct_value);

        // ----- 8. Emit the `pub impl Callable({...}, R) for __closure_N`.
        const callable_proto_id = try interner_mut.intern("Callable");
        const callable_proto_parts = try self.allocSlice(ast.StringId, &.{callable_proto_id});
        const proto_args_tuple = try self.create(ast.TypeExpr, .{
            .tuple = .{ .meta = .{ .span = span }, .elements = try self.allocSlice(*const ast.TypeExpr, param_types) },
        });
        const proto_type_args = try self.allocSlice(*const ast.TypeExpr, &.{ proto_args_tuple, return_type });
        const impl_fns = try self.allocator.alloc(*const ast.FunctionDecl, 1);
        impl_fns[0] = call_decl;
        const impl_decl = try self.create(ast.ImplDecl, .{
            .meta = .{ .span = span },
            .protocol_name = .{ .parts = callable_proto_parts, .span = span },
            .protocol_type_args = proto_type_args,
            .target_type = struct_name,
            .type_params = &.{},
            .functions = impl_fns,
            .is_private = false,
        });
        try self.pending_closure_impls.append(self.allocator, .{ .impl_decl = impl_decl });

        // ----- 9. Build the `%__closure_N{ field: capturedVar, ... }`
        // construction expression. Each field value is a `var_ref` to the
        // captured variable in the ENCLOSING scope (where the closure
        // literal appeared), so the capture is read at construction time.
        const struct_fields = try self.allocator.alloc(ast.StructField, captures.items.len);
        for (captures.items, 0..) |capture_name, idx| {
            struct_fields[idx] = .{
                .name = capture_name,
                .value = try self.create(ast.Expr, .{
                    .var_ref = .{ .meta = .{ .span = span }, .name = capture_name },
                }),
            };
        }
        return try self.create(ast.Expr, .{
            .struct_expr = .{
                .meta = .{ .span = span },
                .struct_name = struct_name,
                .type_args = &.{},
                .type_args_parens_present = false,
                .update_source = null,
                .fields = struct_fields,
            },
        });
    }

    /// Build an `any` type expression at `span`.
    fn anyTypeExpr(self: *Desugarer, span: ast.SourceSpan) !*const ast.TypeExpr {
        const interner_mut: *ast.StringInterner = self.interner;
        const any_id = try interner_mut.intern("any");
        return try self.create(ast.TypeExpr, .{
            .name = .{ .meta = .{ .span = span }, .name = any_id, .args = &.{} },
        });
    }

    /// Build the tuple-slot access expression `args.<index>` — a
    /// `field_access` whose field name is the decimal index, matching how
    /// tuple element access is written and lowered (`tuple.0`, `tuple.1`).
    fn tupleIndexAccess(self: *Desugarer, tuple_name: ast.StringId, index: usize, span: ast.SourceSpan) !*const ast.Expr {
        const interner_mut: *ast.StringInterner = self.interner;
        const index_text = try std.fmt.allocPrint(self.allocator, "{d}", .{index});
        defer self.allocator.free(index_text);
        const index_field_id = try interner_mut.intern(index_text);
        const tuple_ref = try self.create(ast.Expr, .{
            .var_ref = .{ .meta = .{ .span = span }, .name = tuple_name },
        });
        return try self.create(ast.Expr, .{
            .field_access = .{ .meta = .{ .span = span }, .object = tuple_ref, .field = index_field_id },
        });
    }

    /// Rewrite every captured `var_ref` in a statement to `self.<name>`,
    /// leaving all other structure intact. Used to lower a closure body
    /// into its `Callable.call` method, where captures live on the
    /// `self` receiver as fields.
    fn rewriteCaptureStmt(
        self: *Desugarer,
        stmt: ast.Stmt,
        self_name: ast.StringId,
        capture_set: *const std.AutoHashMap(ast.StringId, void),
    ) anyerror!ast.Stmt {
        return switch (stmt) {
            .expr => |e| .{ .expr = try self.rewriteCaptureExpr(e, self_name, capture_set) },
            .assignment => |assign| .{
                .assignment = try self.create(ast.Assignment, .{
                    .meta = assign.meta,
                    .pattern = assign.pattern,
                    .value = try self.rewriteCaptureExpr(assign.value, self_name, capture_set),
                }),
            },
            else => stmt,
        };
    }

    /// Rewrite captured `var_ref`s in an expression to `self.<name>` field
    /// accesses. Recurses through the full expression surface. Nested
    /// closures are walked so a capture shared with an inner closure is
    /// also rewritten (the inner closure, if it escapes, is independently
    /// desugared on a later pass over the rewritten body).
    fn rewriteCaptureExpr(
        self: *Desugarer,
        expr: *const ast.Expr,
        self_name: ast.StringId,
        capture_set: *const std.AutoHashMap(ast.StringId, void),
    ) anyerror!*const ast.Expr {
        switch (expr.*) {
            .var_ref => |vr| {
                if (!capture_set.contains(vr.name)) return expr;
                const self_ref = try self.create(ast.Expr, .{
                    .var_ref = .{ .meta = vr.meta, .name = self_name },
                });
                return try self.create(ast.Expr, .{
                    .field_access = .{ .meta = vr.meta, .object = self_ref, .field = vr.name },
                });
            },
            .binary_op => |bo| return try self.create(ast.Expr, .{
                .binary_op = .{
                    .meta = bo.meta,
                    .op = bo.op,
                    .lhs = try self.rewriteCaptureExpr(bo.lhs, self_name, capture_set),
                    .rhs = try self.rewriteCaptureExpr(bo.rhs, self_name, capture_set),
                },
            }),
            .unary_op => |uo| return try self.create(ast.Expr, .{
                .unary_op = .{
                    .meta = uo.meta,
                    .op = uo.op,
                    .operand = try self.rewriteCaptureExpr(uo.operand, self_name, capture_set),
                },
            }),
            .call => |call| {
                const new_args = try self.allocator.alloc(*const ast.Expr, call.args.len);
                for (call.args, 0..) |arg, idx| {
                    new_args[idx] = try self.rewriteCaptureExpr(arg, self_name, capture_set);
                }
                return try self.create(ast.Expr, .{
                    .call = .{
                        .meta = call.meta,
                        .callee = try self.rewriteCaptureExpr(call.callee, self_name, capture_set),
                        .args = new_args,
                    },
                });
            },
            .field_access => |fa| return try self.create(ast.Expr, .{
                .field_access = .{
                    .meta = fa.meta,
                    .object = try self.rewriteCaptureExpr(fa.object, self_name, capture_set),
                    .field = fa.field,
                },
            }),
            .pipe => |pe| return try self.create(ast.Expr, .{
                .pipe = .{
                    .meta = pe.meta,
                    .lhs = try self.rewriteCaptureExpr(pe.lhs, self_name, capture_set),
                    .rhs = try self.rewriteCaptureExpr(pe.rhs, self_name, capture_set),
                },
            }),
            .unwrap => |uw| return try self.create(ast.Expr, .{
                .unwrap = .{ .meta = uw.meta, .expr = try self.rewriteCaptureExpr(uw.expr, self_name, capture_set) },
            }),
            .tuple => |te| {
                const new_elements = try self.allocator.alloc(*const ast.Expr, te.elements.len);
                for (te.elements, 0..) |elem, idx| new_elements[idx] = try self.rewriteCaptureExpr(elem, self_name, capture_set);
                return try self.create(ast.Expr, .{ .tuple = .{ .meta = te.meta, .elements = new_elements } });
            },
            .list => |le| {
                const new_elements = try self.allocator.alloc(*const ast.Expr, le.elements.len);
                for (le.elements, 0..) |elem, idx| new_elements[idx] = try self.rewriteCaptureExpr(elem, self_name, capture_set);
                return try self.create(ast.Expr, .{ .list = .{ .meta = le.meta, .elements = new_elements } });
            },
            .list_cons_expr => |lce| return try self.create(ast.Expr, .{
                .list_cons_expr = .{
                    .meta = lce.meta,
                    .head = try self.rewriteCaptureExpr(lce.head, self_name, capture_set),
                    .tail = try self.rewriteCaptureExpr(lce.tail, self_name, capture_set),
                },
            }),
            .map => |me| {
                const new_fields = try self.allocator.alloc(ast.MapField, me.fields.len);
                for (me.fields, 0..) |field, idx| new_fields[idx] = .{
                    .key = try self.rewriteCaptureExpr(field.key, self_name, capture_set),
                    .value = try self.rewriteCaptureExpr(field.value, self_name, capture_set),
                };
                return try self.create(ast.Expr, .{
                    .map = .{
                        .meta = me.meta,
                        .update_source = if (me.update_source) |src| try self.rewriteCaptureExpr(src, self_name, capture_set) else null,
                        .fields = new_fields,
                    },
                });
            },
            .struct_expr => |se| {
                const new_fields = try self.allocator.alloc(ast.StructField, se.fields.len);
                for (se.fields, 0..) |field, idx| new_fields[idx] = .{
                    .name = field.name,
                    .value = try self.rewriteCaptureExpr(field.value, self_name, capture_set),
                };
                return try self.create(ast.Expr, .{
                    .struct_expr = .{
                        .meta = se.meta,
                        .struct_name = se.struct_name,
                        .type_args = se.type_args,
                        .type_args_parens_present = se.type_args_parens_present,
                        .update_source = if (se.update_source) |src| try self.rewriteCaptureExpr(src, self_name, capture_set) else null,
                        .fields = new_fields,
                    },
                });
            },
            .range => |re| return try self.create(ast.Expr, .{
                .range = .{
                    .meta = re.meta,
                    .start = try self.rewriteCaptureExpr(re.start, self_name, capture_set),
                    .end = try self.rewriteCaptureExpr(re.end, self_name, capture_set),
                    .step = if (re.step) |s| try self.rewriteCaptureExpr(s, self_name, capture_set) else null,
                },
            }),
            .if_expr => |ie| return try self.create(ast.Expr, .{
                .if_expr = .{
                    .meta = ie.meta,
                    .condition = try self.rewriteCaptureExpr(ie.condition, self_name, capture_set),
                    .then_block = try self.rewriteCaptureBlock(ie.then_block, self_name, capture_set),
                    .else_block = if (ie.else_block) |eb| try self.rewriteCaptureBlock(eb, self_name, capture_set) else null,
                },
            }),
            .case_expr => |ce| {
                const new_clauses = try self.allocator.alloc(ast.CaseClause, ce.clauses.len);
                for (ce.clauses, 0..) |clause, idx| new_clauses[idx] = .{
                    .meta = clause.meta,
                    .pattern = clause.pattern,
                    .type_annotation = clause.type_annotation,
                    .guard = if (clause.guard) |g| try self.rewriteCaptureExpr(g, self_name, capture_set) else null,
                    .body = try self.rewriteCaptureBlock(clause.body, self_name, capture_set),
                };
                return try self.create(ast.Expr, .{
                    .case_expr = .{
                        .meta = ce.meta,
                        .scrutinee = try self.rewriteCaptureExpr(ce.scrutinee, self_name, capture_set),
                        .clauses = new_clauses,
                    },
                });
            },
            .block => |blk| return try self.create(ast.Expr, .{
                .block = .{ .meta = blk.meta, .stmts = try self.rewriteCaptureBlock(blk.stmts, self_name, capture_set) },
            }),
            .anonymous_function => |anon| {
                // Rewrite captures the inner closure shares from the outer
                // scope. The inner closure's own params shadow; a captured
                // name that the inner closure rebinds as a param must NOT
                // be rewritten there. Fork the capture set minus the inner
                // params.
                if (anon.decl.clauses.len == 0) return expr;
                const inner = anon.decl.clauses[0];
                var inner_captures = try capture_set.clone();
                defer inner_captures.deinit();
                for (inner.params) |param| try self.removePatternBindingsFromSet(param.pattern, &inner_captures);
                const new_body: ?[]const ast.Stmt = if (inner.body) |b|
                    try self.rewriteCaptureBlock(b, self_name, &inner_captures)
                else
                    null;
                const new_clauses = try self.allocator.alloc(ast.FunctionClause, anon.decl.clauses.len);
                new_clauses[0] = .{
                    .meta = inner.meta,
                    .params = inner.params,
                    .return_type = inner.return_type,
                    .refinement = inner.refinement,
                    .body = new_body,
                    .raises = inner.raises,
                };
                for (anon.decl.clauses[1..], 1..) |c, idx| new_clauses[idx] = c;
                const new_decl = try self.create(ast.FunctionDecl, .{
                    .meta = anon.decl.meta,
                    .name = anon.decl.name,
                    .name_expr = anon.decl.name_expr,
                    .clauses = new_clauses,
                    .visibility = anon.decl.visibility,
                });
                return try self.create(ast.Expr, .{
                    .anonymous_function = .{ .meta = anon.meta, .decl = new_decl },
                });
            },
            .type_annotated => |ta| return try self.create(ast.Expr, .{
                .type_annotated = .{
                    .meta = ta.meta,
                    .expr = try self.rewriteCaptureExpr(ta.expr, self_name, capture_set),
                    .type_expr = ta.type_expr,
                },
            }),
            .raise_expr => |re| return try self.create(ast.Expr, .{
                .raise_expr = .{ .meta = re.meta, .value = try self.rewriteCaptureExpr(re.value, self_name, capture_set) },
            }),
            // Literals, references that cannot name a captured value
            // (struct_ref, function_ref, attr_ref), intrinsics, quote
            // forms, error-pipe/try-rescue (not produced inside a closure
            // body before this pass), and poison are returned unchanged.
            else => return expr,
        }
    }

    /// Rewrite captures in a statement block.
    fn rewriteCaptureBlock(
        self: *Desugarer,
        stmts: []const ast.Stmt,
        self_name: ast.StringId,
        capture_set: *const std.AutoHashMap(ast.StringId, void),
    ) anyerror![]const ast.Stmt {
        const new_stmts = try self.allocator.alloc(ast.Stmt, stmts.len);
        for (stmts, 0..) |stmt, idx| {
            new_stmts[idx] = try self.rewriteCaptureStmt(stmt, self_name, capture_set);
        }
        return new_stmts;
    }

    /// Remove every binding a pattern introduces from a capture set — used
    /// so a nested closure's own parameters shadow outer captures of the
    /// same name during capture rewriting.
    fn removePatternBindingsFromSet(
        self: *Desugarer,
        pattern: *const ast.Pattern,
        set: *std.AutoHashMap(ast.StringId, void),
    ) anyerror!void {
        switch (pattern.*) {
            .bind => |b| _ = set.remove(b.name),
            .wildcard, .literal => {},
            .tuple => |t| for (t.elements) |sub| try self.removePatternBindingsFromSet(sub, set),
            .list => |l| for (l.elements) |sub| try self.removePatternBindingsFromSet(sub, set),
            .list_cons => |lc| {
                for (lc.heads) |sub| try self.removePatternBindingsFromSet(sub, set);
                try self.removePatternBindingsFromSet(lc.tail, set);
            },
            .map => |m| for (m.fields) |field| try self.removePatternBindingsFromSet(field.value, set),
            .struct_pattern => |sp| for (sp.fields) |field| try self.removePatternBindingsFromSet(field.pattern, set),
            .pin => {},
            .paren => |p| try self.removePatternBindingsFromSet(p.inner, set),
            .binary => |bp| for (bp.segments) |seg| {
                if (seg.value == .pattern) try self.removePatternBindingsFromSet(seg.value.pattern, set);
            },
            .tagged_union_variant => |tv| if (tv.payload) |payload| try self.removePatternBindingsFromSet(payload, set),
        }
    }

    fn desugarStructUpdate(
        self: *Desugarer,
        original: ast.StructExpr,
        source: *const ast.Expr,
        override_fields: []const ast.StructField,
    ) anyerror!*const ast.Expr {
        // Look up the struct definition from the scope graph
        const struct_decl = self.findStructDecl(original.struct_name) orelse {
            // Can't find the struct definition — pass through unchanged.
            // The HIR builder will report the proper error.
            return try self.create(ast.Expr, .{
                .struct_expr = .{
                    .meta = original.meta,
                    .struct_name = original.struct_name,
                    .update_source = source,
                    .fields = override_fields,
                },
            });
        };

        // Build a complete field list including inherited fields.
        // For each field, either use the override value or generate
        // a field_access on the source.
        var all_fields: std.ArrayList(ast.StructField) = .empty;
        const all_def_fields = self.collectAllStructFields(struct_decl);

        for (all_def_fields) |def_field| {
            // Check if this field is overridden
            var override_value: ?*const ast.Expr = null;
            for (override_fields) |of| {
                if (of.name == def_field.name) {
                    override_value = of.value;
                    break;
                }
            }

            if (override_value) |value| {
                try all_fields.append(self.allocator, .{
                    .name = def_field.name,
                    .value = value,
                });
            } else {
                // Generate source.field_name
                const field_access = try self.create(ast.Expr, .{
                    .field_access = .{
                        .meta = original.meta,
                        .object = source,
                        .field = def_field.name,
                    },
                });
                try all_fields.append(self.allocator, .{
                    .name = def_field.name,
                    .value = field_access,
                });
            }
        }

        return try self.create(ast.Expr, .{
            .struct_expr = .{
                .meta = original.meta,
                .struct_name = original.struct_name,
                .update_source = null,
                .fields = try all_fields.toOwnedSlice(self.allocator),
            },
        });
    }

    /// Find a struct declaration by struct name from the scope graph.
    fn findStructDecl(self: *const Desugarer, struct_name: ast.StructName) ?*const ast.StructDecl {
        const graph = self.graph orelse return null;
        if (struct_name.parts.len == 0) return null;
        const struct_name_id = struct_name.parts[struct_name.parts.len - 1];

        if (graph.resolveTypeByName(struct_name_id)) |type_id| {
            if (type_id < graph.types.items.len) {
                const entry = graph.types.items[type_id];
                if (entry.kind == .struct_type) return entry.kind.struct_type;
            }
        }

        const name_str = self.interner.get(struct_name_id);
        for (graph.types.items) |entry| {
            if (entry.kind == .struct_type) {
                if (std.mem.eql(u8, self.interner.get(entry.name), name_str))
                    return entry.kind.struct_type;
            }
        }
        return null;
    }

    /// Collect ALL fields for a struct, including inherited fields from
    /// the extends chain. Returns fields in order: parent fields first,
    /// then own fields (matching the TypeStore's field ordering).
    fn collectAllStructFields(self: *const Desugarer, decl: *const ast.StructDecl) []const ast.StructFieldDecl {
        const graph = self.graph orelse return decl.fields;

        if (decl.parent) |parent_name_id| {
            // Find parent struct
            if (graph.resolveTypeByName(parent_name_id)) |parent_type_id| {
                if (parent_type_id < graph.types.items.len) {
                    const parent_entry = graph.types.items[parent_type_id];
                    if (parent_entry.kind == .struct_type) {
                        const parent_decl = parent_entry.kind.struct_type;
                        const parent_fields = self.collectAllStructFields(parent_decl);
                        // Merge: parent fields + own fields
                        var all_fields: std.ArrayListUnmanaged(ast.StructFieldDecl) = .empty;
                        all_fields.appendSlice(self.allocator, parent_fields) catch return decl.fields;
                        all_fields.appendSlice(self.allocator, decl.fields) catch return decl.fields;
                        return all_fields.toOwnedSlice(self.allocator) catch decl.fields;
                    }
                }
            }
        }

        return decl.fields;
    }

    // ============================================================
    // Unwrap desugaring: expr! → case expr { {:ok, v} -> v, {:error, e} -> panic(e) }
    // ============================================================

    fn desugarUnwrap(self: *Desugarer, inner: *const ast.Expr, meta: ast.NodeMeta) !*const ast.Expr {
        const v_name = try self.interner.intern("__unwrap_val");
        const e_name = try self.interner.intern("__unwrap_err");
        const ok_name = try self.interner.intern("ok");
        const error_name = try self.interner.intern("error");

        // Pattern {:ok, v}
        const ok_pattern = try self.create(ast.Pattern, .{
            .tuple = .{
                .meta = meta,
                .elements = try self.allocSlice(*const ast.Pattern, &.{
                    try self.create(ast.Pattern, .{ .literal = .{ .atom = .{ .meta = meta, .value = ok_name } } }),
                    try self.create(ast.Pattern, .{ .bind = .{ .meta = meta, .name = v_name } }),
                }),
            },
        });

        // Pattern {:error, e}
        const error_pattern = try self.create(ast.Pattern, .{
            .tuple = .{
                .meta = meta,
                .elements = try self.allocSlice(*const ast.Pattern, &.{
                    try self.create(ast.Pattern, .{ .literal = .{ .atom = .{ .meta = meta, .value = error_name } } }),
                    try self.create(ast.Pattern, .{ .bind = .{ .meta = meta, .name = e_name } }),
                }),
            },
        });

        // Body for ok: v
        const ok_body = try self.allocSlice(ast.Stmt, &.{
            .{ .expr = try self.create(ast.Expr, .{ .var_ref = .{ .meta = meta, .name = v_name } }) },
        });

        // Body for error: panic(e)
        const e_ref = try self.create(ast.Expr, .{ .var_ref = .{ .meta = meta, .name = e_name } });
        const error_body = try self.allocSlice(ast.Stmt, &.{
            .{ .expr = try self.create(ast.Expr, .{
                .panic_expr = .{ .meta = meta, .message = e_ref },
            }) },
        });

        const clauses = try self.allocSlice(ast.CaseClause, &.{
            .{ .meta = meta, .pattern = ok_pattern, .type_annotation = null, .guard = null, .body = ok_body },
            .{ .meta = meta, .pattern = error_pattern, .type_annotation = null, .guard = null, .body = error_body },
        });

        return try self.create(ast.Expr, .{
            .case_expr = .{
                .meta = meta,
                .scrutinee = inner,
                .clauses = clauses,
            },
        });
    }

    // ============================================================
    // String interpolation desugaring
    // ============================================================

    fn desugarStringInterpolation(self: *Desugarer, si: *const ast.StringInterpolation) !*const ast.Expr {
        var result: ?*const ast.Expr = null;

        for (si.parts) |part| {
            const part_expr: *const ast.Expr = switch (part) {
                .literal => |str_id| try self.create(ast.Expr, .{
                    .string_literal = .{ .meta = si.meta, .value = str_id },
                }),
                .expr => |expr| blk: {
                    const desugared = try self.desugarExpr(expr);
                    // Wrap in `Stringable.to_string(value)` — the
                    // protocol's HIR-level dispatch picks the right
                    // per-type impl (Integer, Atom, Bool, Float,
                    // String, or any user impl) at compile time based
                    // on `value`'s static type. The protocol struct
                    // name is the only stdlib name the desugarer
                    // hardcodes here, mirroring how `for` desugars
                    // through `Enumerable.next`.
                    const interner_mut: *ast.StringInterner = @constCast(self.interner);
                    const proto_name = try interner_mut.intern("Stringable");
                    const fn_name = try interner_mut.intern("to_string");
                    const proto_parts = try self.allocator.alloc(ast.StringId, 1);
                    proto_parts[0] = proto_name;
                    const proto_ref = try self.create(ast.Expr, .{
                        .struct_ref = .{
                            .meta = si.meta,
                            .name = .{ .parts = proto_parts, .span = si.meta.span },
                        },
                    });
                    const callee = try self.create(ast.Expr, .{
                        .field_access = .{
                            .meta = si.meta,
                            .object = proto_ref,
                            .field = fn_name,
                        },
                    });
                    break :blk try self.create(ast.Expr, .{
                        .call = .{
                            .meta = si.meta,
                            .callee = callee,
                            .args = try self.allocSlice(*const ast.Expr, &.{desugared}),
                        },
                    });
                },
            };

            if (result) |prev| {
                result = try self.create(ast.Expr, .{
                    .binary_op = .{
                        .meta = si.meta,
                        .op = .concat,
                        .lhs = prev,
                        .rhs = part_expr,
                    },
                });
            } else {
                result = part_expr;
            }
        }

        return result orelse try self.create(ast.Expr, .{
            .string_literal = .{
                .meta = si.meta,
                .value = try self.interner.intern(""),
            },
        });
    }

    // ============================================================
    // For comprehension desugaring
    //
    // for x <- iterable { body }
    // →
    // {
    //   fn __for_N(__state) {
    //     case Enumerable.next(__state) {
    //       {:done, _, _} -> []
    //       {:cont, x, __next} -> [body | __for_N(__next)]
    //     }
    //   }
    //   __for_N(iterable)
    // }
    //
    // The `Enumerable.next/1` call is rewritten to the matching impl's
    // `T.next(state)` at HIR build time via protocol dispatch
    // (see `HirBuilder.protocolDispatchStruct`). This is the single,
    // type-driven dispatch path — no AST-shape special-casing here.
    // Strings remain a separate path because they iterate by byte index
    // rather than through Enumerable.
    // ============================================================

    /// Resolution shape for a for-loop's destructuring slot. When the
    /// loop variable carries a type annotation, the destructure target
    /// becomes an internal bind and a type-asserted rebinding is
    /// prepended to the loop body — pushing the annotation through the
    /// same `pattern = (value :: T)` path that any user-written
    /// destructuring assignment uses. When no annotation is present, the
    /// user's pattern slots in directly with no extra binding.
    const LoopBind = struct {
        /// Pattern that goes into the cons head, cont tuple, or string
        /// per-byte slot.
        pattern: *const ast.Pattern,
        /// Statements prepended to the loop body (empty when no
        /// annotation was given).
        pre_body: []const ast.Stmt,
    };

    fn buildLoopBind(self: *Desugarer, fe: *const ast.ForExpr, meta: ast.NodeMeta) !LoopBind {
        if (fe.var_type_annotation) |ta| {
            const raw_name = try self.interner.intern("__loop_raw");
            const raw_pat = try self.create(ast.Pattern, .{
                .bind = .{ .meta = meta, .name = raw_name },
            });
            const raw_ref = try self.create(ast.Expr, .{
                .var_ref = .{ .meta = meta, .name = raw_name },
            });
            const annotated = try self.create(ast.Expr, .{
                .type_annotated = .{ .meta = meta, .expr = raw_ref, .type_expr = ta },
            });
            const assign = try self.create(ast.Assignment, .{
                .meta = meta,
                .pattern = fe.var_pattern,
                .value = annotated,
            });
            return .{
                .pattern = raw_pat,
                .pre_body = try self.allocSlice(ast.Stmt, &.{.{ .assignment = assign }}),
            };
        }
        return .{
            .pattern = fe.var_pattern,
            .pre_body = &.{},
        };
    }

    /// Build the body-statement slice for a loop arm: optional
    /// type-asserted rebinding followed by the arm's expression.
    fn loopArmBody(self: *Desugarer, pre_body: []const ast.Stmt, body_expr: *const ast.Expr) ![]const ast.Stmt {
        if (pre_body.len == 0) {
            return try self.allocSlice(ast.Stmt, &.{.{ .expr = body_expr }});
        }
        var stmts: std.ArrayList(ast.Stmt) = .empty;
        try stmts.appendSlice(self.allocator, pre_body);
        try stmts.append(self.allocator, .{ .expr = body_expr });
        return try stmts.toOwnedSlice(self.allocator);
    }

    fn desugarForExpr(self: *Desugarer, fe: *const ast.ForExpr) !*const ast.Expr {
        // Inside a macro body, leave the `for` expression intact: the
        // macro engine evaluates it directly via
        // `forComprehensionIntrinsic` in `src/macro_eval.zig`. Lifting
        // it into a runtime `__for_N` helper would emit a private
        // function whose body still references compile-time intrinsics
        // (`elem`, `list_at`, etc.), which the type checker
        // would then flag as missing functions. We still recurse into
        // the iterable, filter, and body so any nested non-for
        // desugaring (string interpolation, pipes the macro engine
        // already handled, etc.) still applies.
        if (self.in_macro_body) {
            const iterable = try self.desugarExpr(fe.iterable);
            const filter = if (fe.filter) |f| try self.desugarExpr(f) else null;
            const body = try self.desugarExpr(fe.body);
            return try self.create(ast.Expr, .{
                .for_expr = .{
                    .meta = fe.meta,
                    .var_pattern = fe.var_pattern,
                    .var_type_annotation = fe.var_type_annotation,
                    .iterable = iterable,
                    .filter = filter,
                    .body = body,
                },
            });
        }

        // All iterables route through the Enumerable protocol. The HIR
        // builder's `protocolDispatchStruct` rewrites `Enumerable.next(state)`
        // to `T.next(state)` based on `state`'s inferred type at HIR build
        // time. Strings dispatch through `lib/string/enumerable.zap` — the
        // earlier hand-rolled byte-index loop was deleted in favour of the
        // unified protocol path so every container iterates the same way.
        return self.desugarForEnumerable(fe);
    }

    /// Desugar for comprehension via the Enumerable protocol. Generates
    /// a recursive helper that scrutinises `Enumerable.next(state)` and
    /// branches on the `{:done, _, _}` / `{:cont, val, next_state}`
    /// shape:
    ///
    ///   fn __for_N(__state) {
    ///     case Enumerable.next(__state) {
    ///       {:done, _, _} -> []
    ///       {:cont, x, __next_state} -> [body | __for_N(__next_state)]
    ///     }
    ///   }
    ///
    /// The `Enumerable.next/1` invocation lowers to the matching impl's
    /// `T.next/1` at HIR build time (`HirBuilder.protocolDispatchStruct`),
    /// based on `__state`'s inferred type. This keeps the desugar pass
    /// type-agnostic — list, range, map, and user-defined Enumerables all
    /// share the same generated shape.
    fn desugarForEnumerable(self: *Desugarer, fe: *const ast.ForExpr) !*const ast.Expr {
        const meta = ast.NodeMeta{ .span = .{ .start = 0, .end = 0 } };

        const name_str = try std.fmt.allocPrint(self.allocator, "__for_{d}", .{self.for_counter});
        self.for_counter += 1;
        const helper_name = try self.interner.intern(name_str);
        const state_name = try self.interner.intern("__state");
        const next_state_name = try self.interner.intern("__next_state");

        const state_ref = try self.create(ast.Expr, .{ .var_ref = .{ .meta = meta, .name = state_name } });

        // Build `Enumerable.next(__state)`. The HIR builder rewrites this
        // protocol call to the concrete impl's `T.next(state)` based on
        // `__state`'s inferred type.
        const enum_id = try self.interner.intern("Enumerable");
        const enum_parts = try self.allocator.alloc(ast.StringId, 1);
        enum_parts[0] = enum_id;
        const mod_ref = try self.create(ast.Expr, .{
            .struct_ref = .{ .meta = meta, .name = .{ .parts = enum_parts, .span = .{ .start = 0, .end = 0 } } },
        });
        const next_field = try self.interner.intern("next");
        const next_callee = try self.create(ast.Expr, .{
            .field_access = .{ .meta = meta, .object = mod_ref, .field = next_field },
        });
        const next_call = try self.create(ast.Expr, .{
            .call = .{ .meta = meta, .callee = next_callee, .args = try self.allocSlice(*const ast.Expr, &.{state_ref}) },
        });

        // Recursive call: __for_N(__next_state)
        const next_state_ref = try self.create(ast.Expr, .{ .var_ref = .{ .meta = meta, .name = next_state_name } });
        const rec_call = try self.create(ast.Expr, .{
            .call = .{ .meta = meta, .callee = try self.create(ast.Expr, .{ .var_ref = .{ .meta = meta, .name = helper_name } }), .args = try self.allocSlice(*const ast.Expr, &.{next_state_ref}) },
        });

        // Body: [body(x) | __for_N(__next_state)]
        const body_d = try self.desugarExpr(fe.body);
        var cons_expr = try self.create(ast.Expr, .{
            .list_cons_expr = .{ .meta = meta, .head = body_d, .tail = rec_call },
        });

        // Filter
        if (fe.filter) |filter| {
            const fd = try self.desugarExpr(filter);
            cons_expr = try self.create(ast.Expr, .{
                .case_expr = .{ .meta = meta, .scrutinee = fd, .clauses = try self.allocSlice(ast.CaseClause, &.{
                    .{ .meta = meta, .pattern = try self.create(ast.Pattern, .{ .literal = .{ .bool_lit = .{ .meta = meta, .value = true } } }), .type_annotation = null, .guard = null, .body = try self.allocSlice(ast.Stmt, &.{.{ .expr = cons_expr }}) },
                    .{ .meta = meta, .pattern = try self.create(ast.Pattern, .{ .literal = .{ .bool_lit = .{ .meta = meta, .value = false } } }), .type_annotation = null, .guard = null, .body = try self.allocSlice(ast.Stmt, &.{.{ .expr = rec_call }}) },
                }) },
            });
        }

        // Tuple pattern: {:done, _, _} -> []
        const done_atom = try self.create(ast.Pattern, .{ .literal = .{ .atom = .{ .meta = meta, .value = try self.interner.intern("done") } } });
        const wildcard1 = try self.create(ast.Pattern, .{ .wildcard = .{ .meta = meta } });
        const wildcard2 = try self.create(ast.Pattern, .{ .wildcard = .{ .meta = meta } });
        const done_pat = try self.create(ast.Pattern, .{
            .tuple = .{ .meta = meta, .elements = try self.allocSlice(*const ast.Pattern, &.{ done_atom, wildcard1, wildcard2 }) },
        });
        const empty_list = try self.create(ast.Expr, .{ .list = .{ .meta = meta, .elements = &.{} } });

        // Tuple pattern: {:cont, <user-pattern>, __next_state} -> [body | rec]
        // When no annotation is present, the user's pattern slots
        // directly into the second tuple element. With `:: T`, the slot
        // becomes an internal bind and the cont-arm body opens with a
        // type-asserted re-binding through the standard assignment
        // pipeline (see buildLoopBind).
        const cont_atom = try self.create(ast.Pattern, .{ .literal = .{ .atom = .{ .meta = meta, .value = try self.interner.intern("cont") } } });
        const loop_bind = try self.buildLoopBind(fe, meta);
        const val_bind = loop_bind.pattern;
        const next_bind = try self.create(ast.Pattern, .{ .bind = .{ .meta = meta, .name = next_state_name } });
        const cont_pat = try self.create(ast.Pattern, .{
            .tuple = .{ .meta = meta, .elements = try self.allocSlice(*const ast.Pattern, &.{ cont_atom, val_bind, next_bind }) },
        });

        // case Enumerable.next(state) { {:done, _, _} -> []; {:cont, x, next} -> [body | rec] }
        const case_expr = try self.create(ast.Expr, .{
            .case_expr = .{
                .meta = meta,
                .scrutinee = next_call,
                .clauses = try self.allocSlice(ast.CaseClause, &.{
                    .{ .meta = meta, .pattern = done_pat, .type_annotation = null, .guard = null, .body = try self.allocSlice(ast.Stmt, &.{.{ .expr = empty_list }}) },
                    .{ .meta = meta, .pattern = cont_pat, .type_annotation = null, .guard = null, .body = try self.loopArmBody(loop_bind.pre_body, cons_expr) },
                }),
            },
        });

        // Leave return type unannotated — the type checker propagates the
        // body's `[BodyType]` shape via inferred_signatures so the helper
        // matches whatever element type the comprehension body produces.
        const clause = ast.FunctionClause{
            .meta = meta,
            .params = try self.allocSlice(ast.Param, &.{
                .{ .meta = meta, .pattern = try self.create(ast.Pattern, .{ .bind = .{ .meta = meta, .name = state_name } }), .type_annotation = null },
            }),
            .return_type = null,
            .refinement = null,
            .body = try self.allocSlice(ast.Stmt, &.{.{ .expr = case_expr }}),
        };

        const func_decl = try self.create(ast.FunctionDecl, .{
            .meta = meta,
            .name = helper_name,
            .clauses = try self.allocSlice(ast.FunctionClause, &.{clause}),
            .visibility = .private,
        });
        try self.pending_helpers.append(self.allocator, .{ .priv_function = func_decl });

        // Call: __for_N(iterable)
        const iterable = try self.desugarExpr(fe.iterable);
        return try self.create(ast.Expr, .{
            .call = .{ .meta = fe.meta, .callee = try self.create(ast.Expr, .{ .var_ref = .{ .meta = meta, .name = helper_name } }), .args = try self.allocSlice(*const ast.Expr, &.{iterable}) },
        });
    }

    // ============================================================
    // Import resolution (Elixir-style)
    //
    // Resolves bare function calls against the current struct's
    // imports. If `name/arity` is found in an imported struct
    // (including auto-imported Kernel), returns the source struct
    // name so the call can be rewritten as a qualified call.
    // ============================================================

    fn resolveImportedStruct(self: *const Desugarer, name: ast.StringId, arity: u32) ?ast.StructName {
        const graph = self.graph orelse return null;
        const mod_scope_id = self.current_struct_scope orelse return null;
        const scope = graph.getScope(mod_scope_id);

        // Check if the function is defined locally in this struct — if so,
        // don't rewrite (local definitions shadow imports).
        const key = scope_mod.FamilyKey{ .name = name, .arity = arity };
        if (scope.function_families.get(key) != null) return null;

        // Check imports (includes auto-imported Kernel)
        for (scope.imports.items) |imp| {
            if (graph.findStructScope(imp.source_struct)) |imp_scope_id| {
                const imp_scope = graph.getScope(imp_scope_id);
                if (imp_scope.function_families.get(key) != null) {
                    if (graph.passesImportFilter(imp.filter, key)) {
                        return imp.source_struct;
                    }
                }
            }
        }
        return null;
    }

    // ============================================================
    // Allocation helpers
    // ============================================================

    /// Wrap a `String`-valued expression in a `%RuntimeError{message: ...}`
    /// struct literal. Backs the Phase 1.4 `raise "string"` shorthand: the
    /// `raise_expr` desugar normalises a bare string value into a concrete
    /// `RuntimeError` Error value so the rest of the pipeline sees a uniform
    /// `raise <Error>` shape. `RuntimeError` is the stdlib default error
    /// type defined in `lib/error.zap`.
    fn wrapStringInRuntimeError(self: *Desugarer, string_value: *const ast.Expr, meta: ast.NodeMeta) !*const ast.Expr {
        const runtime_error_name = try self.interner.intern("RuntimeError");
        const message_field_name = try self.interner.intern("message");

        const parts = try self.allocSlice(ast.StringId, &.{runtime_error_name});
        const fields = try self.allocSlice(ast.StructField, &.{
            .{ .name = message_field_name, .value = string_value },
        });

        return try self.create(ast.Expr, .{
            .struct_expr = .{
                .meta = meta,
                .struct_name = .{ .parts = parts, .span = meta.span },
                .update_source = null,
                .fields = fields,
            },
        });
    }

    /// Build the `Kernel.do_raise(<error_value>)` call that backs the
    /// Phase 1.4 `raise` keyword. `do_raise/1` (lib/kernel.zap) is the
    /// Error-aware abort: it extracts `Error.message`/`Error.kind` from the
    /// value and aborts non-zero. Constructed in desugar so the call is
    /// type-checked and monomorphized through the normal path.
    fn buildDoRaiseCall(self: *Desugarer, error_value: *const ast.Expr, meta: ast.NodeMeta) !*const ast.Expr {
        const kernel_name = try self.interner.intern("Kernel");
        const do_raise_name = try self.interner.intern("do_raise");

        // `Kernel` is a struct (type) name — it must be a `struct_ref`, not
        // a `var_ref`, so call resolution treats
        // `field_access(struct_ref(Kernel), do_raise)` as a static struct
        // function call rather than a dynamic field access (which would
        // lower to a `call_closure` and fail to emit).
        const kernel_parts = try self.allocSlice(ast.StringId, &.{kernel_name});
        const kernel_ref = try self.create(ast.Expr, .{
            .struct_ref = .{
                .meta = meta,
                .name = .{ .parts = kernel_parts, .span = meta.span },
            },
        });
        const callee = try self.create(ast.Expr, .{
            .field_access = .{ .meta = meta, .object = kernel_ref, .field = do_raise_name },
        });
        const args = try self.allocSlice(*const ast.Expr, &.{error_value});
        return try self.create(ast.Expr, .{
            .call = .{ .meta = meta, .callee = callee, .args = args },
        });
    }

    // ============================================================
    // Function-reference → callable closure (eta-expansion)
    //
    // A named function reference (`&Struct.fn/n` or bare `&fn/n`) passed as
    // a call argument denotes a *callable value* — `Enum.map(list, &parse/1)`
    // is `Enum.map(list, fn(x) { parse(x) })`. Without this rewrite the
    // reference lowers to the reflective first-class `Function` struct
    // (`{struct, name, arity}` from `lib/function.zap`), which carries no
    // function pointer and so cannot be invoked through the `call_closure`
    // path the higher-order callee uses. Worse, its struct type fails to
    // unify with the callback's `fn(a) -> b` parameter ("expects callable,
    // got Function").
    //
    // The fix eta-expands the reference into an anonymous function whose
    // body forwards to the referenced function:
    //
    //   &Struct.fn/2  →  fn(__fnref_arg_0 :: P0, __fnref_arg_1 :: P1) -> R {
    //                       Struct.fn(__fnref_arg_0, __fnref_arg_1)
    //                    }
    //
    // The synthetic parameter and return type annotations are copied
    // verbatim from the referenced function's declared clause signature
    // (resolved through the scope graph the desugarer already holds), so
    // the closure type-checks exactly like a hand-written one, the
    // monomorphizer specialises the forwarded call normally, and — crucially
    // for effect-polymorphism — a `raises` row on the referenced function
    // flows through the forwarded call into the enclosing function's row,
    // catchable by a surrounding `try`/`rescue` with NO per-error-type
    // specialisation of the combinator.
    //
    // This rewrite fires only in argument position (a `function_ref` that is
    // a direct element of a `CallExpr.args`). A reference used reflectively
    // — returned where the `Function` struct is expected, bound to a
    // `Function`-typed name — is not a call argument and keeps the struct
    // shape (the `.function_ref` passthrough below and in `inferExpr`).
    // ============================================================

    /// Locate the declared `FunctionClause` a function reference resolves to,
    /// using the scope graph. Mirrors the resolution the type checker and
    /// HIR builder perform: an explicit `&Struct.fn/n` resolves in the named
    /// struct's scope; a bare `&fn/n` resolves in the current struct scope
    /// and then through its imports (auto-imported Kernel included). Returns
    /// `null` when the target cannot be resolved here (e.g. an external or
    /// not-yet-collected function) — the caller then leaves the reference
    /// untouched so the normal pipeline reports any genuine error.
    fn resolveFunctionRefClause(self: *const Desugarer, fr: ast.FunctionRefExpr) ?ast.FunctionClause {
        const graph = self.graph orelse return null;
        const lookup_arity: u32 = fr.arity;
        const key = scope_mod.FamilyKey{ .name = fr.function, .arity = lookup_arity };

        if (fr.struct_name) |struct_name| {
            const struct_scope_id = graph.findStructScope(struct_name) orelse return null;
            const struct_scope = graph.getScope(struct_scope_id);
            const family_id = struct_scope.function_families.get(key) orelse return null;
            return clauseFromFamily(graph, family_id);
        }

        const current_scope_id = self.current_struct_scope orelse return null;
        const current_scope = graph.getScope(current_scope_id);
        if (current_scope.function_families.get(key)) |family_id| {
            return clauseFromFamily(graph, family_id);
        }
        for (current_scope.imports.items) |imp| {
            if (graph.findStructScope(imp.source_struct)) |imp_scope_id| {
                const imp_scope = graph.getScope(imp_scope_id);
                if (imp_scope.function_families.get(key)) |family_id| {
                    if (graph.passesImportFilter(imp.filter, key)) {
                        return clauseFromFamily(graph, family_id);
                    }
                }
            }
        }
        return null;
    }

    fn clauseFromFamily(graph: *const scope_mod.ScopeGraph, family_id: scope_mod.FunctionFamilyId) ?ast.FunctionClause {
        const family = graph.getFamily(family_id);
        if (family.clauses.items.len == 0) return null;
        const clause_ref = family.clauses.items[0];
        if (clause_ref.clause_index >= clause_ref.decl.clauses.len) return null;
        return clause_ref.decl.clauses[clause_ref.clause_index];
    }

    /// Eta-expand a function reference used in argument position into an
    /// anonymous function that forwards to the referenced function. Returns
    /// the original `function_ref` expression untouched when the referenced
    /// clause cannot be resolved here (external/uncollected) so the rest of
    /// the pipeline still sees a well-formed reference.
    fn etaExpandFunctionRefArg(self: *Desugarer, fr: ast.FunctionRefExpr) !*const ast.Expr {
        const clause = self.resolveFunctionRefClause(fr) orelse
            return self.create(ast.Expr, .{ .function_ref = fr });

        const arity: usize = fr.arity;
        if (clause.params.len < arity) {
            return self.create(ast.Expr, .{ .function_ref = fr });
        }

        const meta = fr.meta;

        // Build the synthetic parameter list and the forwarding call's
        // argument list in lockstep: param `__fnref_arg_i :: <declared Pi>`
        // and argument `var_ref(__fnref_arg_i)`.
        const params = try self.allocator.alloc(ast.Param, arity);
        const forward_args = try self.allocator.alloc(*const ast.Expr, arity);
        for (0..arity) |idx| {
            const param_name_text = try std.fmt.allocPrint(self.allocator, "__fnref_arg_{d}", .{idx});
            const param_name = try self.interner.intern(param_name_text);
            const bind_pattern = try self.create(ast.Pattern, .{
                .bind = .{ .meta = meta, .name = param_name },
            });
            params[idx] = .{
                .meta = meta,
                .pattern = bind_pattern,
                .type_annotation = clause.params[idx].type_annotation,
                .ownership = clause.params[idx].ownership,
                .ownership_explicit = clause.params[idx].ownership_explicit,
                .default = null,
            };
            forward_args[idx] = try self.create(ast.Expr, .{
                .var_ref = .{ .meta = meta, .name = param_name },
            });
        }

        // Reconstruct the forwarding callee exactly as the reference named
        // it: an explicit struct qualifier becomes
        // `field_access(struct_ref(Struct), fn)`; a bare reference becomes
        // `var_ref(fn)` so the `.call` desugar arm's import resolution
        // qualifies it the same way a hand-written bare call would.
        const forward_callee = if (fr.struct_name) |struct_name| blk: {
            const struct_ref = try self.create(ast.Expr, .{
                .struct_ref = .{ .meta = meta, .name = struct_name },
            });
            break :blk try self.create(ast.Expr, .{
                .field_access = .{ .meta = meta, .object = struct_ref, .field = fr.function },
            });
        } else try self.create(ast.Expr, .{
            .var_ref = .{ .meta = meta, .name = fr.function },
        });

        const forward_call = try self.create(ast.Expr, .{
            .call = .{ .meta = meta, .callee = forward_callee, .args = forward_args },
        });

        const body_stmts = try self.allocSlice(ast.Stmt, &.{.{ .expr = forward_call }});
        const synthetic_clause = ast.FunctionClause{
            .meta = meta,
            .params = params,
            .return_type = clause.return_type,
            .refinement = null,
            .body = body_stmts,
            .raises = clause.raises,
        };
        const decl = try self.create(ast.FunctionDecl, .{
            .meta = meta,
            .name = try self.interner.intern("__fnref_eta"),
            .clauses = try self.allocSlice(ast.FunctionClause, &.{synthetic_clause}),
            .visibility = .private,
        });

        // The forwarding body still needs its own desugaring (e.g. bare-call
        // import qualification). Route it through `desugarFunctionDecl` so
        // the synthetic anonymous function is indistinguishable from a
        // user-written one downstream.
        return self.create(ast.Expr, .{
            .anonymous_function = .{
                .meta = meta,
                .decl = try self.desugarFunctionDecl(decl),
            },
        });
    }

    fn create(self: *Desugarer, comptime T: type, value: T) !*const T {
        const ptr = try self.allocator.create(T);
        ptr.* = value;
        return ptr;
    }

    fn allocSlice(self: *Desugarer, comptime T: type, items: []const T) ![]const T {
        const slice = try self.allocator.alloc(T, items.len);
        @memcpy(slice, items);
        return slice;
    }
};

