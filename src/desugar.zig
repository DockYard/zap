const Parser = @import("parser.zig").Parser;

test "pipe is desugared during macro expansion, not desugar" {
    // Pipe desugaring now happens in the macro engine (Phase 4).
    // The desugar pass just passes pipe nodes through.
    const source =
        \\pub struct Test {
        \\  pub fn foo(x) {
        \\    x |> bar(1)
        \\  }
        \\}
    ;

    var arena = std.heap.ArenaAllocator.init(std.testing.allocator);
    defer arena.deinit();
    const alloc = arena.allocator();

    var parser = Parser.init(alloc, source);
    defer parser.deinit();
    const program = try parser.parseProgram();

    var desugarer = Desugarer.init(alloc, parser.interner, null);
    const desugared = try desugarer.desugarProgram(&program);

    // Pipe should still be a pipe (desugar doesn't transform it anymore)
    const func = desugared.structs[0].items[0].function;
    const body = func.clauses[0].body.?;
    try std.testing.expectEqual(@as(usize, 1), body.len);
    try std.testing.expect(body[0].expr.* == .pipe);
}

test "desugar no-op on simple expressions" {
    const source =
        \\pub struct Test {
        \\  pub fn add(x :: i64, y :: i64) -> i64 {
        \\    x + y
        \\  }
        \\}
    ;

    var arena = std.heap.ArenaAllocator.init(std.testing.allocator);
    defer arena.deinit();
    const alloc = arena.allocator();

    var parser = Parser.init(alloc, source);
    defer parser.deinit();
    const program = try parser.parseProgram();

    var desugarer = Desugarer.init(alloc, parser.interner, null);
    const desugared = try desugarer.desugarProgram(&program);

    try std.testing.expectEqual(@as(usize, 1), desugared.structs.len);
    const func = desugared.structs[0].items[0].function;
    const body = func.clauses[0].body.?;
    try std.testing.expect(body[0].expr.* == .binary_op);
}
